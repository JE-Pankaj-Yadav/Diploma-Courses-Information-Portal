                        <h2>What is a Diploma in Chemical Engineering?</h2>
                        
                        <p>Diploma in chemical engineering is one of the choices available in the numerous engineering diplomas available in the country and as the name suggests this diploma deals with teaching the basics of Chemical Engineering.</p>
                        
                        <ul>
                        	<li>It is a 3-year long course with 2 semesters each semester.</li>
                        	<li>It involves teaching about physical sciences and chemical sciences just like microbiology, biochemistry and biology applying together.</li>
                        	<li>This academic course's main goal is application of chemical sciences in various streams of industry where the conversion from raw materials to newer transformed products take place.</li>
                        	<li>It is a comprehensive study of applying mathematics and economic calculations to analyse the possibilities in regulating the sector and developing refined products.</li>
                        	<li>Its curriculum covers applied chemistry, thermodynamics, chemical technology, heat transfer and much more giving a broader perspective to the field of students.</li>
                        </ul>
                        
                        <p>After finishing the course, the students can enter into production, research laboratories, analysts, petroleum refineries etc. Students who get a diploma in chemical engineering can also enrol into the second year of the respective engineering branch after writing an entrance exam for the same.</p>   
                        <h2>Why Study?</h2>
                        
                        <p>Chemical engineering has a very good scope in the future because it is one of the few branches that bridges the gap between physics and chemistry. Diploma level of engineering teaches all the basics of the engineering branch.</p>
                        
                        <ul>
                        	<li>It teaches applying physics, chemical sciences and biological sciences together.</li>
                        	<li>The course contains study of conversion of raw materials into newer products and insights into industries that work on this.</li>
                        	<li>Chemical engineering also teaches candidates recycling forms and reformation methods for cleaning chemicals exited out by the industries which is why the branch has a heavy demand in the ever- growing recycling industry.</li>
                        	<li>It also focuses on communication and innovation which can help opting for lucrative opportunities in the chemical engineering field.</li>
                        </ul>
                        
                        <p>With the above-mentioned statements, a student opting into the field can carry out deeper research into the field and if interested, they can choose the new&nbsp;budding engineering field in the country.</p>
                        
                        <h2>Who Should Study?</h2>
                        
                        <ul>
                        	<li>Candidates who have interest in studying engineering and its related field should take up the course.</li>
                        	<li>Students who wish to build upon their knowledge of different topics of Chemical Science and physics such as pulp and paper, petrochemicals, food processing, specialty chemicals, microelectronics, electronic and advanced materials, polymers etc in higher secondary level should take up the course.</li>
                        	<li>Students who have a career planned in chemistry, biochemistry, materials science and information technology should take up the course.</li>
                        	<li>Ethical and passionate candidates who want to Contribute meaningful Advancements to the Society should take up the course.</li>
                        	<li>This course is also effective for candidates who wish to gain a certificate in Chemical engineering in a comparatively shorter period of time, and be job ready.</li>
                        </ul>
                        
                        <h2>When to Study?</h2>
                        
                        <ul>
                        	<li>Students can join BTech directly from the second year after completion of the course.</li>
                        	<li>Students get jobs easily in the government and Private sector.</li>
                        	<li>Students learn electronic and advanced materials, polymers etc.</li>
                        	<li>The students also have a chance to join the Food Processing Industry to create food additives that can be used in processed food to create better flavors that make foods more attractive to consumers.</li>
                        	<li>Students become trained and have the skills to start their own initiative as Specialty Chemicals and start their own research lab.</li>
                        </ul>
                        <h2>Diploma in Chemical Engineering Admission Process</h2>
                        
                        <p>The students who are interested in getting into the Diploma in Chemical Engineering course should have cleared the 10th board exams with at-least 45% marks or above in Maths, Science and also English.</p>
                        
                        <ul>
                        	<li>The admission process is totally based on the Merit of the candidate.</li>
                        	<li>Every college releases their own merit list cut off marks.</li>
                        	<li>Candidates can apply through the online or offline mode through the official website of the respective college.</li>
                        	<li>All the important details regarding dates, admission and of the names of shortlisted candidates will be notified on the website through the website and through mail.</li>
                        </ul>
                        <h2>Eligibility</h2>
                        
                        <p>The eligibility criteria for the program are as follows:</p>
                        
                        <ul>
                        	<li>Applicants should have qualified their Class Xth examination or its equivalent with a minimum of 45% marks in every subject along with science and mathematics.</li>
                        	<li>The candidates who are supposed to have studied English as one of their subjects in class Xth are also eligible for admission.</li>
                        </ul>
