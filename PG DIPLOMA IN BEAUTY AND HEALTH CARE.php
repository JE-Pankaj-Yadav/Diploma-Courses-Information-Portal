                        <p>Cosmetology (from Greek κοσμητικός, kosmētikos, "beautifying";[1] and -λογία, -logia) is the study and application of beauty treatment. Branches of specialty include hairstyling, skin care, cosmetics, manicures/pedicures, non-permanent hair removal such as waxing and sugaring, and permanent hair removal processes such as electrology and intense pulsed light (IPL).</p>
