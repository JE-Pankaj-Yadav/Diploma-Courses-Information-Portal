                        <h2>SOMTHING ABOUT PG DIPLOMA IN RETAIL MANAGEMENT</h2>
                        <p><b>Post Graduate Diploma in Retail Management (PGDM-RM)</b> is a 1-year distance learning course or 2 years full-time course. It mainly focuses on the fundamentals of retail management and marketing practices. The minimum eligibility requirements for this postgraduate diploma is a Bachelorâ€™s degree in any discipline.</p>
                        <p>Retail is the sale of goods and services to consumers, in contrast to wholesaling, which is sale to business or institutional customers. A retailer purchases goods in large quantities from manufacturers, directly or through a wholesaler, and then sells in smaller quantities to consumers for a profit. Retailers are the final link in the supply chain from producers to consumers.</p>
                        <p>Retail markets and shops have a very ancient history, dating back to antiquity. Some of the earliest retailers were itinerant peddlers. Over the centuries, retail shops were transformed from little more than "rude booths" to the sophisticated shopping malls of the modern era. In the digital age, an increasing number of retailers are seeking to reach broader markets by selling through multiple channels, including both bricks and mortar and online retailing. Digital technologies are also affecting the way that consumers pay for goods and services. Retailing support services may also include the provision of credit, delivery services, advisory services, stylist services and a range of other supporting services.</p>

                    </div><br>
                    <p><b>Tuition fee</b> for PGDM-RM ranges from INR 20,000 to 60,000 in distance learning mode. It may go up to INR 5 Lacs if you do it full-time from a reputed college or University.</p>
                    <p>A PGDM-RM graduate has a variety of Career and Job Options. They can get hired in companies like Lifestyle, Big bazaars, Shoppers stop, Reliance Industries, etc. and expect a salary between INR 20,000 to 40,000 per month.  An individual can work as a retail store manager, financial controller, retail sales executive, etc.</p>
                    <h4>ELIGIBILITY</h4>
                    <li>Bachelor's degree holder / Graduate in any discipline from a recognised University.</li>
                    <li>International / SAARC  Graduate from a recognised / accredited University / Institution.</li>
                    <li>Students who have appeared for final year of examination of their bachelor's degree program and are awaiting results can also apply, subject to successfully completing their bachelor's degree program / graduation within the time period specified by SCDL.</li><br>
                    <h4>PGDM-RM: What is the Course About?</h4>
                    <p>PGDM-RM provides a thorough understanding of the concepts and processes involved in retailing and associated sectors. It covers both academic and practical aspects applicable to best practice in the retail business. An individual learns to apply and evaluate apt techniques that are required for successful retailing.</p>
                    <p>PGDM RM Syllabus covers topics that involve basics of retailing, consumer behavior, sale, and marketing management, supply chain network and electronic retailing. It mainly focuses on retail management and marketing.</p><br>
                    <h4>Who should opt for PGDM-RM course?</h4>
                    <p>Anyone who loves being surrounded by brands, wants to become an entrepreneur, possesses managerial skills, creative and professional can opt for this course.</p><br>
                    <h4>Duration of PGDM-RM</h4>
                    <p>The duration of this course varies with different colleges and universities. In some colleges like Birla Institute of Management Technology, Accman Institute of Management and DAV Institute of Management the duration of this course is 2 years.</p>
                    <p>PG Diploma in Retail Management (PGDM-RM) offered by distance learning universities like Symbiosis University is of 1 year.</p>
                    <p>Some colleges also offer this course for 18 months like Asian School of Business Management and Bangalore Management Academy.</p>
