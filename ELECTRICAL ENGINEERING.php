                        <h2>What is a Diploma in Electrical Engineering?</h2>
                        <ul>
	                        <li>This Diploma Electrical Engineering course basically deals with the power used for lighting, heating, cooling, refrigeration and operating home appliances, control systems, electronics, and signal processing.</li>
	                        <li>The study and application of electricity, electromagnetism, and electronics to process information and transmit energy is known as electrical engineering.</li>
	                        <li>The Diploma in Electrical Engineering course mainly focuses on electrical elements and devices which are used to generate electricity from different methods such as Windmills, Nuclear power plants.</li>
	                        <li>In each semester the candidate will study the different subjects that teach analytical, technical and design skills. Electrical engineering enables students to comprehend the many applications of electricity and electronics.</li>
                        </ul>
