                        <h2>Types Diploma in Electrical Engineering</h2>
                        <p>There are mainly two types of courses available one is Full time Diploma in Electrical Engineering (3 years) and other is part time Diploma in Electrical Engineering. Apart from these, a Distance Diploma in Electrical Engineering is available from Institute of Engineering and Management Studies, Shivani College of IT and Management etc.</p>
                        <h2>Full-Time Diploma in Electrical Engineering</h2>
                        <ul>
                            <li>A full time Diploma in Electrical Engineering is a 3 years professional course offered by various government and private institutions.</li>
                            <li>The average fee for a diploma in electrical engineering course ranges between INR 10,000-INR 2, 00,000.</li>
                            <li>The admission in the course is done through the entrance examination such as Odisha DET, Assam PAT, JEXPO, etc.</li>
                            <li>The top colleges offering electrical engineering diplomas are KJ Somaiya Polytechnic, Central Polytechnic College, Swami Vivekananda Group of Institutions and many more.</li>
                        </ul>
                        <h2>Part Time Diploma in Electrical Engineering</h2>
                        <ul>
                            <li>The duration of the part time Diploma in Electrical Engineering is 4 years.</li>
                            <li>The minimum eligibility criterion is that the candidates must pass their 10th or equivalent examination with at least 45% marks.</li>
                            <li>The part time diploma in electrical engineering course is provided to those people who are working and want to improve their skills in the electrical domain.</li>
                            <li>Part time Diploma in Electrical Engineering is provided by the following colleges, University of Kerala, University of Calicut, MS University of Baroda and many more.</li>
                            <li>The average fee for a part time diploma in electrical engineering course is around INR 35,000-INR 50,000.</li>
                        </ul>
