                        <h2>SOMTHING ABOUT ELECTRICAL ENGINEERING</h2>
                        <p>Diploma in Electrical Engineering is a 3 year skill-enabling engineering programme that students can pursue after completing class 10. The course deals with installation, maintenance, troubleshooting, magnetism, control systems, signal processing and telecommunications.</p>
                        <p>The eligibility for Diploma in Electrical Engineering courses is a minimum score of 50% marks in class 10. A relaxation of 5% is provided to the candidate who belongs to reserved categories.</p>
                        <p><b>Electrical engineering</b> is an engineering discipline concerned with the study, design, and application of equipment, devices, and systems which use electricity, electronics, and electromagnetism. It emerged as an identifiable occupation in the latter half of the 19th century after commercialization of the electric telegraph, the telephone, and electrical power generation, distribution, and use.</p>
