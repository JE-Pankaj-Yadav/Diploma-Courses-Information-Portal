                        <h2>SOMTHING ABOUT ELECTRICAL AND ELECTRONICS ENGINEERING [LATERAL ENTRY]</h2>
			            <p><b>Diploma in electrical and electronics engineering:</b> With the advent of technology and an ever-increasing dependence on devices that are meant to improve our standard of living, right from smartphones virtual and augmented reality to the newly found metaverse, the importance of chips, electronics, and electrical equipment is nowhere near the downward trend of the spiral curve. The importance of knowledge in the field will increase manifold in the years to come.</p>
                        <p>Given the growing importance of knowledge and intelligence with respect to electrical and electronics engineering, the demand from the student community to pursue this course is increasing by the day. Also, the demand for quality talent from the private industry and academia to improve research and development in the electrical and electronics space is always on the higher end of the spectrum.</p>
                        <p>With such a scenario in place, it is of no surprise that a diploma in electrical and electronics engineering is a fantastic career opportunity for a student to pursue early in their life. In this particular article in diploma in electrical and electronics engineering, we are going to answer the following questions and try to cover as many topics as possible related to a diploma in electrical and electronics engineering.</p>
                        <h2>What is Diploma in Electrical and Electronics Engineering?</h2>

                        <p>A diploma in electrical and electronics engineering is a 3-year course that a student can pursue after his or her 10th from a recognized board in India</p>
                        <p>The following points will help an aspirant understand what is a diploma in electrical and electronics engineering:</p>
                        <ul>
                            <li>Diploma in electrical and electronics engineering is a three-year course that one can do after 10th standard from any recognized board in India.</li>
                            <li>Electrical engineering is the study and application of magnetism and electromagnetism, electricity, and is a subset of the science of engineering. Ever since the discovery of electricity, the role and application of electricity in our life has been increasing by the day, and electrical and electronics engineering as study has a major contribution in that arena.</li>
                            <li>Right from the manufacturing sector to the service sector and agriculture sector and governmental and non-governmental organizations, the application of electricity and electronics is paramount for proper day-to-day functionalities of these industries.</li>
                            <li>Diploma in electrical and electronics engineering will help a student land a job in any of the industries that have the application of electricity. In other words, an electrical and electronics engineering graduate can work anywhere at any point of time in his or her career because that is the kind of job that is in demand across the globe.</li>
                        </ul>
                        <h2>Who should pursue a Diploma in Electrical and Electronics Engineering?</h2>
                        <p>The following points should be some of the eligibility criteria to pursue electrical and electronic engineering in diploma:</p>

                        <h2>Diploma in Electrical and Electronics Engineering(DEEE) Eligibility</h2>
                        <ul>
                            <li>The candidate should have passed the 10th standard from any recognized board in the country.</li>
                            <li>The candidate should have a minimum aggregate of 35% in his 12th standard.</li>
                            <li>The main subjects of the candidate in the 10th standard should be science and mathematics.</li>
                            <li>Candidate should have studied English as a compulsory subject in his class 10th and should have a good grasp over the communication in the same language.</li>
                        </ul>
                        <p>While the above is the criteria that are set by the Ministry of education of various education boards both in Central and state government, we firmly believe that to pursue a course in diploma in electrical and electronics engineering the candidate should be:</p>
                        <ul>
                            <li>Passionate about electricity, electronics, magnetism, physics Artificial Intelligence and virtual reality along with machine learning.</li>
                            <li>Candidate should be extremely dynamic and curious to expand his or her horizon of knowledge because this is a field that keeps changing and updating by the day. One should be at the tip of their toe to keep learning new things every day.</li>
                            <li>The bend of mind and thought process of an aspirant should be in line with the course pedagogy of a diploma in electrical and electronics engineering.</li>
                            <li>Candidate should be highly determined and dedicated towards studies because not only is the syllabus very vast, the practical learning that one should get outside of the classrooms is extremely important to become well versed in electrical and electronics engineering.</li>
                        </ul>
                        <p>Find course details belonging to different streams all explained clearly so that you can decide whether you will have a bright career or not.</p>

                        <h2>Diploma in Electrical and Electronics Engineering Admission Procedure</h2>
                        <p>The following points should sum up the admission procedure for a diploma in electrical and electronics engineering</p>
                        <ul>
                            <li>Complete 10th standard from a recognized board with a minimum 35% aggregate</li>
                            <li>Apply for colleges that you are aiming for through online applications or paper applications that are provided at that particular college campus</li>
                            <li>Certain colleges, especially private and deemed universities, will have their own separate entrance exams, Students have to clear the exams with a prescribed cut-off to get an admission seat in the college.</li>
                            <li>If the college is asking for a personality test interview, attend the interview.</li>
                        </ul>
                        <h2>Requirements for Diploma in Electrical and Electronics Engineering</h2>
                        <p>The course and structure of the pedagogy is typical in almost all public and private universities across the country for diploma in electrical and electronics engineering. However, the course structure and pedagogy might vary in other countries or it might also vary in certain private institutions within India. What really matters is the methodology of teaching and the extent of advanced laboratory infrastructure that a college has.</p>
                        <ul>
                            <li>The whole course is divided into 6 semesters where each semester will have two laboratory subjects and at least four theoretical subjects</li>
                            <li>At the end of the course, students will have compulsory industrial visits to get first-hand experience of the market</li>
                            <li>In certain universities, students are also expected to complete a mandatory internship program in the company of their choice and also the field of their choice. They shall be assigned mentors or guides from the university to help them sail through the rigorous internship program.</li>
                        </ul>
                        <h2>Diploma in Electrical and Electronics Engineering Jobs</h2>
                        <p>Below points will give an idea for a student on the job market and career growth after their graduation from the course of diploma in electrical and electronics engineering:</p>
                        <p>After completion of diploma in electrical and electronics engineering student can start working in the positions of:</p>
                        <p>1. Engineering trainee<br />
                        2. Assistant engineer<br />
                        3. Project manager<br />
                        4. Editor or research<br />
                        5. Junior lecturer or associate professor</p>
                        <p>The average salary for a diploma in electrical and electronics engineering varies highly depending on the competency of the student, the knowledge level, the location of the institution, and most importantly, the brand of the college that he or she is passing out from. However, on average, students should land a job that pays them or her at least 6 lacs per annum.</p>
                        
