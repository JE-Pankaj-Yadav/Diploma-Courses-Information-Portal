                        <h2>SOMTHING ABOUT MINING ENGINEERING</h2>
                        <p>Diploma in Mining Engineering is a 3-year diploma level course which can be pursued after passing class 10th. <b>A Diploma in Mining Engineering is a course that involves the science and technology of processing and extracting minerals.</b></p>
                        <p><b>Candidates who wish to pursue Diploma in Mining Engineering must have completed the 10th standard with PCM stream. A minimum of 55% aggregate marks is required for them to apply.</b></p>
                        <p>The admission to Diploma in Mining Engineering is based solely on merit basis i.e marks obtained in class 10th or 12th standard. Candidates need to clear the cut-off list released by the colleges.</p>
                        <p>Some of the top colleges offering Diploma in Mining Engineering are Kalinga University, Raipur, Himalayan University, Itanagar, Swami Vivekanand University, Sagar, Government Polytechnic, Nagpur, Annamalai University, Annamalai</p>
                        <p>The average course fee for Diploma in Mining Engineering ranges from INR 19,000 to 45,000.</p>
                        <p>Subjects taught in Diploma in Mining Engineering are Mining Geology, Introduction to Mining, Mining Practices, Surface Mining, Blasting Techniques in Mines, Mine surveying practice, mine machinery, Rock Mechanics and Ground Control, etc.</p>
                        <p>After completing the Diploma in Mining Engineering students can opt for higher studies in the field. Some of the course options available are BE/BTech in Mining Engineering, MTech Mining Engineering, Doctor of Philosophy in Mining Engineering, and more.</p>
                        <p>Some of the top recruiters of Diploma in Mining Engineering National Institute of Rock Mechanics, Atlas Copco India, KPGM Bengaluru, South Eastern Coalfields, several institutions, etc. The average starting salary offered after doing a diploma in mining engineering is INR 6,56,000.</p>
