                        <h2>Diploma in Mining Engineering: What is it about?</h2>
                        <p>Diploma in Mining Engineering is a 3-year diploma level course divided into 6 semesters. This course is for those students who have analytical minds and are hard working. The candidate desiring to do this course must have analytical thinking. Along with this, the candidate must be an industrious person, travel lover, adventurous, and must have a team spirit.</p>
                        <p>Diploma in Mining Engineering aims to teach students about extracting, discovering, and processing minerals. They also learn about the art of developing, building, maintaining, and surveying mines. In this course subjects like Mining Geology, Introduction to Mining, Mining Practices, Surface Mining, Blasting Techniques in Mines, Mine surveying practice, Mine Machinery, Rock Mechanics, and Ground Control are taught. Students can opt for higher studies after completing the course such as BE/BTech in Mining engineering, Master's in mining engineering, etc.</p>
