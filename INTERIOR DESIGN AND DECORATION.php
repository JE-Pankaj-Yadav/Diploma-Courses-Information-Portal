                        <p><b>BA Interior Design:</b> Bachelor of Arts Interior Design or BA Interior Design is an 3 years Architectural course that involves the construction, designing and maintenance of housing and space management skills. The basic emphasis of BA Interior Design is laid on the idea of how a blank space or an area can be transformed into a space suitable for any individual pertaining to their needs and demands. The admission to this course is done on the basis of merit list or entrance test (CET) conducted by the institutes itself</p>
                        
