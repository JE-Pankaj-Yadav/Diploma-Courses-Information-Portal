                        <h2>SOMTHING ABOUT INTERIOR DESIGN AND DECORATION</h2>
                        <p>Diploma in Interior Design is a 1 or 2 year long course depending on the university where candidates learn about various aspects of designing a House, office or interior of any place they desire. Interior Design is a skilled field in Interior sectors.</p>
                        <p>The eligibility criteria of candidates to apply in different colleges or universities are at least 55 % marks in the H.S.C examination in any stream from a recognised board. Admission to Diploma in Interior Design is offered on the marks scored in the entrance examination. The entrance examination is followed by a round of personal interview.In some cases admission is provided on the basis of marks scored in the qualifying examination. See: Interior Design courses</p>
                        <p>The top Diploma in Interior Design colleges are Calicut University, Mumbai University, IMS Design and Innovation Academy, Poddar Group of Institutions, Jaipur ,etc </p>
                        <p>The starting salary after Diploma in Interior Design is about INR 2 lakhs to 8 lakhs per annum. Candidates who want to become Interior Application Developer, Layout Analyst, Interior Marketing Analyst, Front End Interior Developers mostly pursue this course.</p>
                        <p>Interior design is the art and science of enhancing the interior of a building to achieve a healthier and more aesthetically pleasing environment for the people using the space. An interior designer is someone who plans, researches, coordinates, and manages such enhancement projects. Interior design is a multifaceted profession that includes conceptual development, space planning, site inspections, programming, research, communicating with the stakeholders of a project, construction management, and execution of the design.</p>
