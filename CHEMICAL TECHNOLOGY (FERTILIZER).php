                        <h2>SOMTHING ABOUT CHEMICAL TECHNOLOGY (FERTILIZER)</h2>
                        <p>A fertilizer (American English) or fertiliser (British English; see spelling differences) is any material of natural or synthetic origin that is applied to soil or to plant tissues to supply plant nutrients. Fertilizers may be distinct from liming materials or other non-nutrient soil amendments. Many sources of fertilizer exist, both natural and industrially produced.[1] For most modern agricultural practices, fertilization focuses on three main macro nutrients: Nitrogen (N), Phosphorus (P), and Potassium (K) with occasional addition of supplements like rock dust for micronutrients. Farmers applying these fertilizers in a variety of ways: through dry or pelletized or liquid application processes, using large agricultural equipment or hand-tool methods.</p>
                        <p>Historically fertilization came from natural or organic sources: compost, animal manure, human manure, harvested minerals, crop rotations and byproducts of human-nature industries (i.e. fish processing waste, or bloodmeal from animal slaughter). However, starting in the 19th century, after innovations in plant nutrition, an agricultural industry developed around synthetically created fertilizers. This transition was important in transforming the global food system, allowing for larger-scale industrial agriculture with large crop yields. In particular, nitrogen-fixing chemical processes such as the Haber process at the beginning of the 20th century, amplified by production capacity created during World War II led to a boom in using nitrogen fertilizers. In the latter half of the 20th century, increased use of nitrogen fertilizers (800% increase between 1961 and 2019) have been a crucial component of the increased productivity of conventional food systems (more than 30% per capita) as part of the so-called "Green Revolution"</p>
