                        <p>The duration of Diploma Fertilizer Technology course varies depending as per the last qualification of the candidates. The course duration ranges from 6 months for professionals to 2 years for 12th passed candidates. In case of distance learning mode, the duration varies as the candidates can sit for examination as and when they deem fit.</p>
                        <h2>Further study options</h2>
                        <p>Candidates can go for higher degree programs in the same field such as Graduation and Post Graduation. Post Graduate Diploma in Fertilizer Technology is an option for them. Graduate candidates can go for Management degree to get job in management field. If they want to go in the field of teaching, then they can pursue B.Ed course.</p>
