                        <h2>PG DIPLOMA IN ADVERTISING & PUBLIC RELATIONS</h2>
                        <p>A one-year post-graduate diploma course in Advertising & Public Relations in JIMMC addresses the growing demand for professionals in the Advertising and PR industry. Most of JIMMCâ€™s Ad/PR department alumni hold positions of leadership in the Ad world, corporate sector and PR industry today. Opportunities in this field can be accessed from the fact that no business across the world can run without Advertising and Public Relations. Diploma in Advertising & Public Relations from JIMMC will enable you to land jobs in the Advertising Agencies, PR Agencies, Event Management Companies. On the client side, the student can be employed in Account Planning, Client Servicing, Creative, Film Making, Media Planning & Operation, Public Relations and Event Management.</p>
                        <p><b>Advertising</b> is a marketing communication that employs an openly sponsored, non-personal message to promote or sell a product, service or idea. Sponsors of advertising are typically businesses wishing to promote their products or services. Advertising is differentiated from public relations in that an advertiser pays for and has control over the message. It differs from personal selling in that the message is non-personal, i.e., not directed to a particular individual. Advertising is communicated through various mass media, including traditional media such as newspapers, magazines, television, radio, outdoor advertising or direct mail; and new media such as search results, blogs, social media, websites or text messages. The actual presentation of the message in a medium is referred to as an advertisement (advert or ad for short).</p>
                        <p><b>Public relations (PR) </b>is the practice of managing and disseminating information from an individual or an organization (such as a business, government agency, or a nonprofit organization) to the public in order to affect their public perception. Public relations (PR) and publicity differ in that PR is controlled internally, whereas publicity is not controlled and contributed by external parties. Public relations may include an organization or individual gaining exposure to their audiences using topics of public interest and news items that do not require direct payment. The exposure mostly is media-based. This differentiates it from advertising as a form of marketing communications. Public relations aims to create or obtain coverage for clients for free, also known as earned media, rather than paying for marketing or advertising also known as paid media. But in the early 21st century, advertising is also a part of broader PR activities.</p>
