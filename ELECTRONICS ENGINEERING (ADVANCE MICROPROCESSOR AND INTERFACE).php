                        <h2>Diploma in Electronics Engineering: What is it about?</h2>

                        <p>Diploma in Electronics Engineering focuses on designing and testing ICs, inductors, capacitors, and resistors. In this course, students will learn to design electronic circuits, computer systems, electric wires, and also develop control systems. These candidates are considered to deal with the problems that occur with systems such as electrical machines and electric power transmission and find solutions for an issue occurred in the electrical and electronic machines.</p>

                        <p>This course has major topics such as fundamentals and principles of Maths, Computer Science, Physics, and Project Management. In this course, there are sub-disciplines of Electrical and Electronics Engineering include power, Telecommunications, electronics, Signal processing, control, and microelectronics Instrumentation and Computers.</p>

                        <p>This course also helps to learn electrical machinery, collection, future problems of generation, transmission and electrical controls in factories, electric motors and their control, distribution of electrical energy, and transformers. An objective of this course is to provide electrical power to candidates.</p>
                        <h2>Diploma in Electronics Engineering: Eligibility</h3>

                        <p>Candidates who have completed 10th examination with Mathematics and Science stream or an equivalent examination from a recognized board are eligible for Diploma in Electronics Engineering course. Candidates who have passed the 10th examination with 55% of marks are also eligible for this course. The percentage may vary from one college to another.</p>

                        <p>Eligible candidates may apply for this course through online on the official website of the respective college or get an application directly from the admission office of the respective college.</p>
