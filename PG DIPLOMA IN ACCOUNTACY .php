                        <h2>SOMTHING ABOUT PG DIPLOMA IN ACCOUNTACY (WITH COMPUTERISED ACCOUNTS & TAXATION)</h2>
                        <p>Postgraduate Diploma in Accountancy is a 1-year commerce stream course. It is available both part-time and full-time at the convenience of the candidate. Specializing in financial accounting and bookkeeping, the course is designed to impart research skills as well as holistic research focus qualifications.</p>
                        <p>Accounting, also known as accountancy, is the measurement, processing, and communication of financial and non financial information about economic entities such as businesses and corporations. Accounting, which has been called the "language of business", measures the results of an organization's economic activities and conveys this information to a variety of stakeholders, including investors, creditors, management, and regulators. Practitioners of accounting are known as accountants. The terms "accounting" and "financial reporting" are often used as synonyms.</p>
                        <p>The Master of Accountancy (MAcc, MAcy, or MAccy), alternatively Master of Science in Accounting (MSA or MSAcy) or Master of Professional Accountancy (MPAcy, MPAcc or MPAc), is a graduate professional degree designed to prepare students for public accounting; academic-focused variants are also offered.</p>
                        
                    </div><br>
                    <h4>Eligibility</h4>
                    <p>Eligibility to pursue the course is a minimum of 50% marks in aggregate or equivalent in Bachelor’s Degree from any recognized University/ College.</p>
                    <p>The admission to BMLT is based on the candidate’s scores in the entrance exam which are CAT, CMAT, XAT, etc. Some other colleges offer admissions based on the merit score in the last qualifying examination.</p>
                    <p>The average course fee is between INR 10,000 to INR 4,00,000 based on the institution and the location of the institute. </p>
                    <p>PGD in Accountancy course syllabus includes Strategic management, Advanced cost accounting, Economics, seminars, etc. </p>
                    <p>The graduates of the PG Diploma in Accountancy course can opt for jobs including Investments Analyst, Financial Analyst, etc, and are in huge demand in today’s public as well as private sectors. The initial salary package is around INR 50,000 to INR 4 LPA.</p><br>
                    <h4>All About PGD in Accountancy</h4>
                    <p>The one-year course provides quality education to candidates in the accounting sector. The course serves the purpose of handling business numbers and rationally presenting them. The course is very important among the CA aspirants. The duration of the course is flexible for candidates pursuing distance learning. </p><br>
                    <h4>Why study PGD in Accountancy?</h4>
                    <p>Candidates pursuing this degree can become efficient managers and leaders in the business world. Accounting and financial management is an evergreen course because it is the backbone for all business enterprises. Undoubtedly the course provides distinct knowledge about accounting relationships. Besides, it also inculcates skills that suggest excellent business communication. IPS candidates with the skills for granting as external auditors, taxation consultants, chartered accountants, and other high salary jobs. A brief about why a commerce candidate should pursue PGD in accountancy are here:</p>
                    <li><b>Job opportunities:</b> The course provides distinct job opportunities in the accounting and management sectors. Although there are other specialized courses for all the jobs out there, accounting is a specialty that holds jobs almost in every sector. A manager specializing in accountancy looks more attractive to business officials. </li>
                    <li><b>Salary structure:</b> PGD in accountancy tends to hold high salary jobs such as Chartered Accountancy. The average range of initial salary of a PGD Accountancy degree holder is 50,000 to 4 lacs. Due to heavy competence, candidates sometimes can get a salary as per their demands. </li>
                    <li><b>Safety net:</b> the course acts as a safety net as accounting will never go out of fashion. Business enterprises are always on the lookout for chartered accountants or accounting analysts with fresh knowledge. As experience in the field grows, the value of the candidate also accelerates. Besides, fresher candidates are immediately hired by the top companies.</li>
                    <li><b>Specialization:</b> There are not many candidates opting for accountancy. Because of the tough subjects and highly competitive exams, not every candidate gets the opportunity to excel in the field. Because of such stats, specialization is highly valued and demanded.</li><br>
                    <h4>PGD in Accountancy: Admission Process</h4>
                    <p>Admission procedure in PG diploma in accountancy varies from University to University. Most of them consider the marks obtained at graduation level or merit. At the same time, others depend on the entrance examination (multiple choice question pattern) for selection. Some common examinations such as CAT/CMAT can also be taken into consideration. </p><br>
                    <h4>PGD in Accountancy: Eligibility Criteria</h4>
                    <li>Candidates with a UG degree from any recognized University/college with a minimum of 50% aggregate marks are eligible.</li>
                    <li>More preference is given to candidates with Accountancy or any other commerce-based subject. </li>
                    <li>Candidates in the final year of the UG course are also eligible to apply. </li>
