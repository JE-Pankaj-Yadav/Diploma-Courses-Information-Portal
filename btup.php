                        <h2>BOARD OF TECHNICAL EDUCATION , UTTAR PRADESH, LUCKNOW</h2>

                        <p><b>Uttar Pradesh Board of Technical Education</b> (UPBTE or BTEUP) is the board which provides technical education to students in <a href="https://en.wikipedia.org/wiki/Uttar_Pradesh" title="Uttar Pradesh">Uttar Pradesh</a>, India. It was set up in May 1958 as 'State Board Of Technical Education and Training', the name was changed to 'Board Of Technical Education' in 1962.</p>
                        <p>The board offers around 60 courses of one year, two year, three year and four-year duration in different disciplines . There are over 30 trades offered in group A, such as like computer science, IT, mechanical, electrical, electronics, automobile, and civil. The functions of the board declaring the exam schedule, organizing semester exams and declaring their results. Other functions includes affiliating institutions, prescribing their standards of building and equipment.</p>
                        <p>There are more than 80 colleges affiliated with BTEUP that offer courses in group A. Other groups offer courses in textiles, home science, agriculture, and others. As of Jan 2019 there were over 800 institutes affiliated to UPBTE, many of them being government polytechnics.</p>
