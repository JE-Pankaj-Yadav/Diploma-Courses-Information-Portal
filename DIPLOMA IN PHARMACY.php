                        <h2>Why Choose Diploma in Pharmacy Course?</h2>
                        <p>Diploma in Pharmacy [D.Pharm.] course provides registered pharmacy qualification for practicing pharmacy profession under the Pharmacy Act. Many graduates pursue careers in a hospital pharmacy or community pharmacy where they are directly involved in patient care working in partnership with physicians. Pharmacists are the key person to dispense the medication to the patient which requires special registration status from government of India.</p>
                        <p>D. Pharm course graduates are eligible to open an online pharmacy which is a demanding business in India in the field of pharmacy. The course aims to trained qualified candidates to fill orders under a pharmacist's supervision, enter patient’s information, identify prescriptions, and process paperwork for orders, understand and apply drug and medical terminology, as well as basic pharmacology. The curriculum of the diploma course is designed to teach students to understand drug dosage calculations, regularly used pharmacy computer systems, compounding, pharmacy practices and procedures.</p>
