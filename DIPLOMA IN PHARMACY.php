                        <h2>SOMTHING ABOUT DIPLOMA IN PHARMACY</h2>
                        <p>In India, Diploma in Pharmacy (often shortened as DPharm or DPharma) is an entry-level tertiary pharmacy credential. It is obtained following two-year of training. Students can enroll in the course after successfully completing higher secondary education in science stream with physics, chemistry and either biology or maths as subjects. After obtaining the diploma, registration with the pharmacy council is required to be a registered pharmacist. A D.Pharm holder can also enroll for a professional (undergraduate) degree course of Bachelor of Pharmacy via lateral entry scheme.</p>
                        <p>A diploma holder can be employed as a registered pharmacist in a hospital or pharmacy dispensing drugs and pharmaceuticals. It is mandatory that at least one person employed in a pharmacy be a qualified and registered pharmacist.</p>
                        <p>The scope of pharmacy practice includes more traditional roles such as compounding and dispensing of medications, and it also includes more modern services related to health care, including clinical services, reviewing medications for safety and efficacy, and providing drug information. Pharmacists, therefore, are the experts on drug therapy and are the primary health professionals who optimize the use of medication for the benefit of the patients.</p>
                        <h2>About Diploma in Pharmacy (D.Pharm)</h2>
                        <p>Diploma in Pharmacy is a short-range course for aspirants who want to start their career in the field of pharmacy. Instead of doing long term courses in pharmacy domain, D. Pharmacy course is the ideal choice for the student who wants a quick start in their pharmacy career. Pursuing D. Pharm can be a good beginning to understand the fundamentals of pharmacy programme and then pursue advanced courses like B. Pharm & Pharm.D.</p>
                        <p>Diploma in Pharmacy covers fundamental Pharmacy Education including the application of chemistry in the pharma industry, theoretical & practical concepts of biochemistry, pharmacology and toxicology. The course enables the students to study about different chemical salts, their application and uses in medicine. The programme holds fine significance in the pharmaceutical industry.</p>
                        <h2>D. Pharm. Course Eligibility:</h2>
                        <p>Minimum eligibility qualification is 10+2 level of education in science stream, from a recognized educational board, for admission into the Diploma in Pharmacy [D.Pharm.] course in any college. A minimum aggregate score of 35% (Passing marks) at the 10+2 level is also necessary. The cut-off percentage of marks differs as per the University/College.</p>
