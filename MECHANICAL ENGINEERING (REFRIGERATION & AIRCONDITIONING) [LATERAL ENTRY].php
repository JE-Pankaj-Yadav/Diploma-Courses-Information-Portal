                        <h2>SOMTHING ABOUT MECHANICAL ENGINEERING REFRIGERATION AND AIRCONDITIONING [LATERAL ENTRY]</h2>
                        <p>Refrigeration and air conditioning (RAC) engineers design, install, repair and maintain the cooling systems. RAC engineers may have to do this for large and complex building and factories. RAC engineers cater to these needs basically; heating, cooling, humidifying, dehumidifying, cleaning, ventilating and effectiveness. If one aspires for a career which is different from others, which helps to sustain energy and offers one a variety of work in varied locations then this is the right career option for one. RAC engineers have formed an association, IRHACE (Institute of Refrigeration, Heating and Air Conditioning) to promote economic well-being and quality of life in every nation.</p>
                        <h2>Career possibilities of the job</h2>
                        <p>One will enter the organization at level one and gradually as they tend to gain experience they will be promoted to the level of senior associates. There is almost no place left where a refrigerator or air conditioner is not being used. So the employment opportunities pertaining to this field are abundant. One can choose to survey buildings prior to fitting of equipments, diagnose and fix faults or work with renewable energy technologies to improve efficiency and minimize the production of harmful gases in the atmosphere.</p>
                        <h2>Industries hiring</h2>
                        <p>The companies subject the freshly hired candidates to trainings which impart them with the necessary skills to work effectively and in line with the industry standards. One can have an edge over the competitors if he or she has impressive communication skills. They will be better able to interact with the customers in order to assess their requirements and to advice them the ways for reducing the energy consumption. In places like USA, Far East and Australia ample opportunities of employment are available. Companies like Carter Synergy, Parsons Brinchkerhoff, GEA Denco are some of the prominent employers across the globe for RAC engineers.</p>
                        <h2>Approximate salary to be expected at the entry level</h2>
                        <p>On a normal basis one may have to work for 40-45 hours a week and if an emergency situation arises then one may be called up on the weekends. But if one are working with big companies then one may have to fly to distant locations in order to look after clients situated far off. If one is a trainee then one can expect to receive enough money for his survival in good conditions. If one is newly qualified and hired from the college then he or she may receive double the amount than that received by the trainee. Experienced resources that are in supervisory roles are very highly paid.</p>
                        <h2>Pre qualifications required to join the stream</h2>
                        <ul>
                            <li>One must have good technical skills, ability to abide by instructions and practice rules of safety.</li>
                            <li>One must pass the +2 level with physics, chemistry and mathematics.</li>
                            <li>One must clear entrance tests and complete the course successfully with no backlogs. The course can be of diploma/graduation type.</li>
                            <li>Certifications may also be required to be submitted as a proof of one’s abilities. One may start as an apprentice and then after getting trained one may rise onto high level.</li>
                            <li>Certain institutions may impose an age bar for entry into the apprenticeship schemes. So do check with the colleges before one apply for them.</li>
                        </ul>
