                        <li>The minimum age requirement is 15 years. An entrance exam is held for admission to the diploma course.</li>
                        <h2>How is Diploma in Architectural Assistantship Course Beneficial?</h2>
                        <li>Government Architect's offices of different states and universities are a good wide job providing area.</li>
                        <li>Government engineering offices like P.W.D., Public Health, Irrigation, Town Planning Corporation offices, etc. can get a job as Draughtsman. Teaching jobs are also evergreen fields for them. </li>
                        <li>Self-employment is a good area for them and a large percentage of diploma holders in Architectural Assistantship find employment with private architects. </li>
                        <li>Some percentage of students may find employment in state housing boards, urban development authorities.</li>
