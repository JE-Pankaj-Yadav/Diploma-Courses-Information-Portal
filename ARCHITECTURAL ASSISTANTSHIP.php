                        <h2>SOMTHING ABOUT ARCHITECTURAL ASSISTANTSHIP</h2>
                        <p>Architectural assistants act in a support role to the architectural project team freeing up qualified architects from simple tasks. Their responsibilities are varied and may range from involvement in meetings, to preparing drawings, schedules and reports, undertaking site visits, carrying out surveys and so on.</p>
                        <p>Architectural management falls into two distinct parts, office management and project management (Brunton et al., 1964; Emmitt, 1999a & 1999b). Office management provides an overall framework within which individual projects are commissioned, designed and completed. Both parts have the same objectives but are typically addressed by separate management systems. Office management involves the allocation and financing of resources, principally premises, trained staff and computer systems, and on establishing and charging appropriate fees for the services rendered. Project management focuses on timescales, developing a design from initial concept to working drawings, and managing the construction process (see for example Emmitt, 2014).</p>
                        <h2>Diploma in Architectural Assistantship Eligibility</h2>
