                                <h2>Course Subjects</h2>										   
								<p><b>Subjects Covered Under Paint Technology Course</b></p>
                                <p>The subjects that are taught to a student when he or she studies Paint technology are: -</p>
                                <ul>
                                    <li>Liquid Physics</li>
                                    <li>Composition Characteristics</li>
                                    <li>Lipid Production</li>
                                    <li>Oil Characterization</li>
                                    <li>Oil-Bearing Materials</li>
                                    <li>Oil Industry</li>
                                    <li>Oil Refining</li>
                                    <li>Hydrogenation and Modification of Oils</li>
                                </ul>
                                <h2>Careers in Paint Technology</h2>                                									
									<p><b>Careers in Paint Technology</b></p>
                                    <p>The Paint Industry is an industry heavily dependent on other industries where the application of paints is required. These industries are mainly the automobile industry and the real estate industry. From working in research to working in quality assurance, there are many job profiles that a paint technology graduate can fit.</p>
									<h2>Upcoming trends</h2>
										<p><b>Upcoming Trends in Paint Technology</b></p>
                                        <p>There is a lot of innovation happening in the paint and coating industry as people continue to become more aware of the products that they are using and are surrounded by. Some upcoming trends in the Paint Technology sector are: -</p>
                                        <ul>
                                            <li><b>Push for Bio-friendly paints </b> Due to people being more aware of each kind of product that they are using, companies have started doing research and producing bio-friendly products. This helps them create a good image in front of their customer base.</li>
                                            <li><b>Artificial Intelligence and Robotics </b> The concepts of AI and robotics have undergone a great deal of change and transformation. The paint industry is also expected to be affected by the technological revolution as robots specifically built for painting purposes are being tested.</li>
                                            <li><b>Efficient chemical composition </b>All the companies aim to gain an efficient chemical composition, wherein they can get the most out of their product at the lowest cost. This leads to a never-ending race for the company to create the best product at the lowest cost.</li>
                                        </ul>
