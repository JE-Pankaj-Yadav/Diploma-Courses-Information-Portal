                        <h2>SOMTHING ABOUT DIPLOMA IN PAINT TECHNOLOGY</h2>
                        <h2>What is Paint Technology?</h2>
                        <p>Chemical technology is a branch of general engineering, and Paint Technology is a further subsidiary of chemical technology. This makes Paint technology a very niche segment in Engineering and even in Chemical technology. </p>
                        <p>Paint Technology deals with the application, constitution and its usage. Students that study this course are taught about each ingredient that constitutes a paint and how it overall affects the paint. They are taught how paints are created, and the uses of different polymers, pigments and raisins. The major course in Paint Technology is a Bachelor of Technology in Paint Technology (B.Tech in Paint Technology). </p>
                        <h2>Eligibility Criteria (UG & PG) of Paint Technology</h2>
                        <p>To be granted admission in Paint Technology courses, a candidate needs to satisfy the edibility criteria. The eligibility criteria for an aspiring student willing to pursue this course is: -&nbsp;</p>
                        <h2>Eligibility Criteria for Paint Technology at UG Level</h2>
                        <ul>
                            <li>The candidate must have passed Senior Secondary/ 10+2 level from a recognized board.</li>
                            <li>The candidate must have scored at least 50% during graduation from senior Secondary/ 10+2</li>
                            <li>That candidate must have the science stream during his senior Secondary/ 10+2.</li>
                        </ul>
                        <h2>Eligibility Criteria for Paint Technology at PG Level</h2>
                        <ul>
                            <li>The candidate must have passed B.Tech. in Paint Technology</li>
                            <li>The candidate must have scored at least 50% during graduation.</li>
                        </ul>
                        <h2>Entrance Exams for Paint Technology Courses</h2>
                        <ul>
                            <li><b>JEE Main -</b> The Joint Entrance Examination Main (JEE Main) is an entrance examination held at the national level for students who are looking for admission in various engineering and technology courses.</li>
                            <li><b>MHT CET -</b> The Maharashtra Common Entrance Test (MHT CET) is administered by the Directorate of Technical Education, Maharashtra so that aspiring students can be granted admission in engineering courses in the colleges in Maharashtra.</li>
                        </ul>
                        <h2>Merit-based admissions</h2>
                        <p>Some colleges and institutions grant admission to Paint Technology aspiring students based on their performance at the Senior Secondary/ 10+2 level. Students might even get scholarships at these colleges based on their Senior Secondary performance.</p>
                        <h2>Scope of Paint Technology in India and Abroad</h2>
                        <p>The paint and lubrication industry in India and the world for that matter is a very niche industry and is expected to create a similar number of job opportunities for Paint Technology graduates as it has been creating for the past few years.</p>
                        <p>Due to very few people opting to specialize in this area, the demand will always be there. To increase their knowledge in the paint technology field and increase their chances of getting hired, students can also go for Master Technology in Paint Technology (M.Tech in Paint Technology). Going for higher studies increases the employability of the student by a significant margin.</p>
