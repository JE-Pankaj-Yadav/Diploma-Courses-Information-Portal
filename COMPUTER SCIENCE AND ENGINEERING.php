                        <h2>Diploma in Computer Science and Engineering: Admission Process</h2>
                        <p>Admissions in Diploma courses are also done through both merit and entrance exams.</p>
                        <h2>Direct Admission</h2>

                        <ul>
                        	<li>Students need to submit the application form to the colleges or universities they want to take admission.</li>
                        	<li>Students need to perform well in their undergraduate course as the scores obtained in these form the basis in the whole admission process.</li>
                        	<li>Some college's release their cutoff list as well please check if you are eligible in any of the colleges.</li>
                        	<li>If you are eligible then visit the college and submit all the required documents.</li>
                        </ul>
                        <h2>Entrance Exam Based Admission</h2>
                        <p>The steps to apply for entrance exam based admission are mentioned below:</p>
                        <p><b>Step 1- Registration</b>: Students need to register themselves on the institute portal by entering basic details like phone number, mobile number etc.</p>
                        <p><b>Step2</b>- <b>Application Form: </b>Application form needs to fill cautiously so that the entered information is correct.</p>
                        <p><b>Step3-</b> <b>Uploading of documents: </b>All necessary documents need to be scanned and uploaded in the acceptable format on the institute's portal.</p>
                        <p><b>Step4- Payment of Application Fees: </b>Students can pay the application fee either through online mode or offline mode.</p>
                        <p><b>Step5- Downloading of the admit card: </b>Admit cards are released once all applicants are judged for eligibility. Print out of the admit card will be taken and it will be used at the time of entrance examination.</p>
                        <p><b>Step6-</b> <b>Entrance Exam: </b>Prepare for the exam as per syllabus and past papers.</p>
                        <p><b>Step7-</b> <b>Results: </b>Results are announced after a few weeks. If a candidate is successful in qualifying the entrance they can move on to the next round.</p>
                        <p><b>Step8-</b> <b>Counseling and Admission: </b>Counseling is held for students who have cleared the entrance examinations. The student can now take admission in Diploma in Computer Science course.</p>

