                        <h2>SOMTHING ABOUT PG DIPLOMA IN MARKETING AND SALES MANAGEMENT</h2>
                        <P>Marketing management is the organizational discipline which focuses on the practical application of marketing orientation, techniques and methods inside enterprises and organizations and on the management of a firm's marketing resources and activities.</P>
                        <p>Diploma in Sales and Marketing Management is a full-time 1-year program which comes under the field of sales and marketing. The course mainly focuses on marketing as the core business function. The course also deals with various other aspects of marketing management such as consumer behaviour and market research.</p>
                        <p>The minimum eligibility to get admission in this course is a bachelor’s degree or equivalent from any recognised University. </p>
                        <p>The duration of the course is 1 year but some colleges do offer part-time education facilities.</p>
                        <p>The average course fee for a Diploma in Sales and Marketing Management ranges from INR 4,000 to 2,00,000 in India.</p>
                        <p>Admission to Diploma in Sales and Marketing Management is entirely based on merit and no entrance exam is conducted.</p>
                        <p>A Diploma in Sales and Marketing Management Offers great career opportunities with graduates. Some common employment areas are the e-Commerce Sector, Financial Services, Investment Banking, Investment Management Companies, Management Consulting Companies, Marketing Companies, Research & Educational Institutions and Retailing Sector.</p>
                        <p>The average annual salary offered to Diploma in Sales and Marketing Management graduates ranges between INR 2 to 12 lacs per annum.</p><br>

