                        <h2>SOMTHING ABOUT ELECTRONICS ENGINEERING (MICRO ELECTRONICS)</h2>
                        <p>Microelectronics is a subfield of electronics. As the name suggests, microelectronics relates to the study and manufacture (or microfabrication) of very small electronic designs and components. Usually, but not always, this means micrometre-scale or smaller. These devices are typically made from semiconductor materials. Many components of normal electronic design are available in a microelectronic equivalent. These include transistors, capacitors, inductors, resistors, diodes and (naturally) insulators and conductors can all be found in microelectronic devices. Unique wiring techniques such as wire bonding are also often used in microelectronics because of the unusually small size of the components, leads and pads. This technique requires specialized equipment and is expensive.</p>
                        <h2>What is Microelectronics</h2>
						<p>Microelectronics is a field of engineering which deals with the study and manufacturing of small, tiny, even microscopic electronic designs and components.</p>
                        <p>It is a foundation of electronics science and technology, as well as information science and technology in the 21st century, with the development of modern high tech.</p>
                        <p>Microelectronic course subject is usually meant for ambitious and highly driven engineers with great potential due to the course program being difficult.</p>
                        <p>With the microelectronics industry growing and developing continuously over the past several decades has led to the rise in demand for students from this degree course for employment opportunities.</p>
                        <p>Microelectronics engineering is offered in Bachelors of microelectronics engineering (BE BTech) and Masters of microelectronics engineering (ME/M.Tech.) degree courses.</p>
                        <p>As the demand for small and less expensive electronic devices grows, the field continues to expand. The main areas of focus in microelectronics engineering include reliability, research and manufacturing.</p></p>
						<h2>Eligibility Criteria (UG & PG) of Microelectronics</h2>
						<p>Engineering colleges and institutions require certain eligibility from applicants to proceed through the admission process to various engineering courses. There is a difference in the eligibility criteria for both undergraduate and graduate degree courses for microelectronics engineering.</p>
