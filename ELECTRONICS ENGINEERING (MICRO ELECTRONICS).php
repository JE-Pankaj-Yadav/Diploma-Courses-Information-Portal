                        </ul>
                        <h2>For postgraduate degree</h2>
                        <ul>
                            <li>Candidates must have minimum marks of 60% and above for ME/MTech degree courses.</li>
                            <li>They must have completed BE/BTech degree, which is mandatory for pursuing a postgraduate degree in engineering.</li>
                            <li>Must have appeared for entrance exams with the required score as per the guidelines of the university or institute.</li>
                        </ul>
                        <h2>Entrance Exams Microelectronics</h2>
                        <p>With thousands of aspirants in an engineering course, various entrance tests are conducted national, state and university level for admissions to degree courses of engineering, including microelectronics subject. These engineering entrance exams in India provide opportunities for individuals to get admissions into well-known prestigious institutions for pursuing the course.</p>
                        <ul>
                            <li><b>MHT CET</b> Maharashtra common entrance test (MHT CET) is an online mode of exam conducted every year by the Directorate of Technical Education (DTE) to grant admissions for engineering and technology courses (BE/BTech) in the state.</li><br>
                            <li><b>JEE Main</b> JEE Mains is national level entrance exams conducted by National Testing Agency (NTA) every year to grant admission for engineering courses. It is a gateway for admissions to IITs and NITs. It is a computer-based online exam.</li><br>
                            <li><b>VITEEE</b> Vellore Institute of Technology Engineering Examination (VITEEE) is the common entrance test for admissions to VIT Chennai, VIT Andhra Pradesh, VIT Vellore, and VIT Bhopal. It is conducted by Vellore Institute of Technology, and it is a University level examination. The mode of examination is online.</li><br>
                            <li><b>JEE Advanced </b>- JEE Advanced is conducted by IITs of different states in rotation once every year. It is a national level entrance test for admissions to a prestigious institution of IITs in India. It is an entrance test for undergraduate engineering course degrees in IITs. The mode of exam is an online computer-based exam.</li>
                        </ul>
