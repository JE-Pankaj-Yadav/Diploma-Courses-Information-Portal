                        <h2>SOMTHING ABOUT LIATHER TECHNOLOGY FOOTWER (COMPUTER AIDED SHOE DESIGN)</h2>
                        <p>A <b>shoe</b> is an item of footwear intended to protect and comfort the human foot. Shoes are also used as an item of decoration and fashion. The design of shoes has varied enormously through time and from culture to culture, with form originally being tied to function. Though the human foot can adapt to varied terrains and climate conditions, it is still vulnerable to environmental hazards such as sharp rocks and temperature extremes, which shoes protect against. Some shoes are worn as safety equipment, such as steel-toe boots which are required footwear at industrial worksites.</p>
                        <h2>Shoemaking is the process of making footwear.</h2>
                        <p>Originally, shoes were made one at a time by hand, often by groups of shoemakers, or cobblers (also known as cordwainers). In the 18th century, dozens or even hundreds[1] of masters, journeymen and apprentices (both men and women) would work together in a shop, dividing up the work into individual tasks. A customer could come into a shop, be individually measured, and return to pick up their new shoes in as little as a day.[2] Everyone needed shoes, and the median price for a pair was about one dayâ€™s wages for an average journeyman.</p>
                        <p><b>CAD/CAM in the footwear industry</b> is the use of computers and graphics software for designing and grading of shoe upper patterns and, for manufacturing of cutting dies, shoe lasts and sole moulds. CAD/CAM software is a PC-based system, which is made up of program modules. Today, there are 2D and 3D versions of CAD/CAM systems in the shoe industry.</p>
