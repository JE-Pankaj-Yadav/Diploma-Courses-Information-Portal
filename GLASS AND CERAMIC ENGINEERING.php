                        <h2>SOMTHING ABOUT GLASS AND CERAMIC ENGINEERING</h2>
                        <p>Ceramic engineering is the science and technology of creating objects from inorganic, non-metallic materials. This is done either by the action of heat, or at lower temperatures using precipitation reactions from high-purity chemical solutions. The term includes the purification of raw materials, the study and production of the chemical compounds concerned, their formation into components and the study of their structure, composition and properties.</p>
                        <h2>After completing Diploma in Ceramic Technology you can become :</h2>
                        <h2>Glass Engineer - How to become a Glass Engineer</h2>
                        <p><b>Glass Engineer</b> is a professional who focuses on the design of glass, ceramics, and other related composites. In our daily life, the use of glass has been increased many folds in the last few days. Be it utensils in the kitchen, apparatus in clinical laboratories, show pieces in homes or structure of huge building complex’s nothing can be imagined without the use of glass. Due to its large-scale use in the day to day activities, there has been a huge demand for Glass Engineers. </p>
                        <h2>Glass Engineer Eligibility</h2>
                        <p>Aspiring Candidates who want to become Glass Engineer must possess at least a Bachelor’s Degree in Glass Engineering from a good college/university. </p>
                        <p>However, processing a Diploma in Glass Engineering can also help join this field as an Engineer at the junior level.</p>
                        <h2>Glass Engineer Required Skills</h2>
                        <li>The most important skill that a budding Glass Engineer required in today’s scenario is the Creative mind.  </li>
                        <li>Aspiring Glass Engineer must have a solid understanding of the processes and policies of working with many different types of Glass materials.</li>
                        <li>Patience, creativity, detail orientation, and analytical thinking are necessary traits to become a good Glass Engineer</li>
                        <li>He must also possess good communication and interpersonal skills and ability to work as part of a team.</li>
                        <h2>How to become a Glass Engineer?</h2>
                        <p>Students have to follow below-given steps for admission procedure and other its related aspects required to become a Glass Engineer.</p>
                        <h2>Step 1</h2>
                        <p>Students after passing 12th class with subjects like Physics, Chemistry and Mathematics, have to possess a Bachelor’s Degree/Diploma in Engineering with Glass/Ceramic as main subjects.  There are many colleges/universities like Glass Academy Kanchipuram in Tamilnadu, Gorang Institute of Glass Design, Ahmadabad, NID, Ahmadabad etc. both in public and private sectors offering courses for the profession. The admission to these courses is given on the basis of merit list in entrance tests like JEE etc. conducted either on all India level or state level.</p>
                        <h2>Step 2</h2>
                        <p>After Completion of B.E Glass/Ceramic Engineering Course candidates can join some good company of the related field OR one can go for Master’s Degree (M.Tech – Glass and Ceramic Engineering) as there are many employers who consider it as the main qualification for given jobs.</p>
                        <h2>Glass Engineer Job Description</h2>
                        <p>Their main task involves designing and testing new materials that can be used in place of traditional glass in applications where enhanced performance or strength may be required.Glass Engineer is also involved in developing new equipment that can be used to improve the quality glass production process. Some Glass Engineers are also involved in the production of glass-related structures or products.</p>
                        <h2>Glass Engineer Career Prospects</h2>
                        <p>With increasing use of ceramics and glass materials in day to day life the scope of Glass Engineers has increased a lot in recent past. Glass Engineer can find the job in the related field at both Technical as well managerial. Glass Engineer can find the job in Tainted Glass units as well as units making artificial jewellery.</p>
                        <h2>Glass Engineer Salary</h2>
                        <p>Glass Engineers can earn a monthly salary of Rs.40,000 to Rs.50,000 in beginning on an average basis. But this is not the limit; they can earn more after gaining good work experience with the passage of time.</p>
