                        <h2>SOMTHING ABOUT PG DIPLOMA IN CUSTOMER SERVICE MANAGEMENT</h2>
                        <p>PG Diploma in Customer Relationship Management is diploma level course. Generally, the duration of this course is 1-year long and its syllabus is divide in 2-semester. It is an approach to manage a company's interplay with current and potential customers. It is career presenting in nature and provide many job scopes after its successful completion. The main aim of this program is address the growth in service industry and the need for leadership skills in services marketing and managing the customer supplier relationship. It uses data study about customers' history with a company to upgrade business relationships with customers, specifically aim on customer retention and ultimately driving sales growth. With Customer relationship management systems customers are served better on day to day process and with more reliable information their requirement of self service from companies will decrease. </p>
                        <P>Customer relationship management (CRM) is a process in which a business or other organization administers its interactions with customers, typically using data analysis to study large amounts of information.</P>
                        <P>CRM systems compile data from a range of different communication channels, including a company's website, telephone, email, live chat, marketing materials and more recently, social media.[2] They allow businesses to learn more about their target audiences and how to best cater for their needs, thus retaining customers and driving sales growth.[3] CRM may be used with past, present or potential customers. The concepts, procedures, and rules that a corporation follows when communicating with its consumers are referred to as CRM. This complete connection covers direct contact with customers, such as sales and service-related operations, forecasting, and the analysis of consumer patterns and behaviors, from the perspective of the company.</P>
