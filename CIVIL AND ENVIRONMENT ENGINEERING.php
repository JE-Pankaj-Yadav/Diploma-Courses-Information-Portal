                        <h2>SOMTHING ABOUT CIVIL AND ENVIRONMENT ENGINEERING</h2>
                        <p>The Civil Engineering (PHEE) Department is well established with latest Civil Engineering equipments and Environmental Engineering testing devices. The Curriculum is designed keeping in view the entrepreneurial prospects of students. After completing this diploma course, students have diversified job opportunities not only in the field of Civil Engineering (Construction Industry in Private and Public Sector, Self employed as Civil Engineering Contractor, State and Central Public Works Departments, Metro rail corporations etc.) but also in Public Health & Environmental Engineering sectors (State and Central Pollution Control Boards, Jal Boards etc.). Based on employment opportunities and need of the industry, competency level is developed in the students who are sent to various industries to gain practical knowledge and in-depth understanding of problems.</p>
                        <p>Civil Engineering is a professional engineering discipline that deals with the design, construction, and maintenance of the physical and naturally built environment, including works like bridges, roads, canals, dams, and buildings . It is traditionally broken into several sub-disciplines including environmental engineering, geotechnical engineering, structural engineering, transportation Engineering. Civil engineering takes place on all levels in the public sector from municipal through to national governments, and in the private sector from individual homeowners through to international companies.</p>
                        <p>Environmental engineering involves water and air pollution control, recycling, waste disposal, and public health issues as well as knowledge of environmental engineering law. It also includes studies on the environmental impact of proposed construction projects .</p>
