                        <li>Aligarh Muslim University - AMU, Aligarh</li>
                        </ul>
                        <h2>Diploma in Instrumentation & Control Engineering Course Suitability</h2>
                        <ul>
                        <li>They should have critical thinking and problem-solving techniques which may be applied to research and industrial applications are included in the laboratory work.</li>
                        <li>Applicants should be able to maintain equipment such as performing routine maintenance on equipment and determining when and what kind of maintenance is needed.</li>
                        <li>They could also be able to conduct tests and inspections of products, services, or processes to evaluate quality or performance.</li>
                        <li>Candidates could also have reading comprehension skills like understanding written sentences and paragraphs in work related documents.</li>
                        </ul>
                        <h2>How is Diploma in Instrumentation & Control Engineering Course Beneficial?</h2>
                        <ul>
                        <li>The course is beneficial to pursue postgraduate studies in instrumentation science and related areas. Students will be highly-skilled in safety applications, DC electronics and metrology, AC electronics and photonics, industrial electrical systems and motors, digital systems and microcontrollers, hydraulics and pneumatics, and design drafting.</li>
                        <li>They can get jobs in RD units of public and private sector companies as well.</li>
                        <li>They are also required by the Heavy industries such as Thermal Power Stations, Steel Plants, Refineries, and Cement and Fertilizer Plants both in Govt. and private sectors.</li>
                        <li>They can also go for further Master Degree and then for research work.</li>
                        </ul>
