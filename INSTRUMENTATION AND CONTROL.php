                        <h2>SOMTHING ABOUT INSTRUMENTATION AND CONTROL </h2>
                        <p><b>Diploma in Instrumentation & Control Engineering</b> is an undergraduate Instrumentation Engineering course. Instrumentation is the art and science of measurement and control of process variables within a production or manufacturing area. The course includes Digital and Analogue Electronics, Principles of Programmable Systems, Introduction to Computer Architecture, Introduction to Network Technologies, Mechanical Principles, Engineering Materials, Applied Mathematics, Profession Practice, and Electronic Product Design. The course is career orienting in nature that opens many scopes after its completion.</p>
                        <p>Instrumentation and control engineering (ICE) is a branch of engineering that studies the measurement and control of process variables, and the design and implementation of systems that incorporate them. Process variables include pressure, temperature, humidity, flow, pH, force and speed.</p>
                        <p>Control systems engineering activities are multi-disciplinary in nature. They focus on the implementation of control systems, mainly derived by mathematical modeling. Because instrumentation and control play a significant role in gathering information from a system and changing its parameters, they are a key part of control loops.</p>
                        <h2>Diploma in Instrumentation & Control Engineering Eligibility</h2>
                        <ul>
                            <li>The students should have passed the 12th class in Science with a minimum of 50% (45% for SC/ST) or equivalent CGPA.</li>
                        </ul>
                        <h2>Diploma in Instrumentation & Control Engineering Colleges</h2>
                        <ul>
                        <li>Gujarat Technological University, Ahmedabad</li>
