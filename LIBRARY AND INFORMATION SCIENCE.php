                        <h2>SOMTHING ABOUT LIBRARY AND INFORMATION SCIENCE</h2>
                        <p>Library and information science(s) or studies (LIS) is an interdisciplinary field of study that deals generally with organization, access, collection, and protection/regulation of information, whether in physical (e.g. art, legal proceedings, etc.) or digital forms.</p>
                        <p>In spite of various trends to merge the two fields, some consider the two original disciplines, library science and information science, to be separate. However, it is common today to use the terms synonymously or to drop the term "library" and to speak about information departments or I-schools. There have also been attempts to revive the concept of documentation and to speak of Library, information and documentation studies (or science).</p>
                        <h2>Relations between library science, information science and LIS</h2>
                        <p>The common ground between library science and information science, which is a strong one, is in the sharing of their social role and in their general concern with the problems of effective utilization of graphic records. But there are also very significant differences in several critical respects, among them in: selection of problems addressed and in the way they were defined; theoretical questions asked and frameworks established; the nature and degree of experimentation and empirical development and the resulting practical knowledge/competencies derived;  tools and approaches used; and  the nature and strength of interdisciplinary relations established and the dependence of the progress and evolution of interdisciplinary approaches. All of these differences warrant the conclusion that librarianship and information science are two different fields in a strong interdisciplinary relation, rather than one and the same field, or one being a special case of the other.</p>
                        <h2>Diploma in Library and Information Science: Eligibility Criteria</h2>
                        <li>The applicant must have passed the 10+2 or equivalent level examination with at least 50 percent of the marks from a recognised state or central board (relaxable for reserved category applicants).</li>
                        <li>Candidates should not have any backlogs; before entering the diploma course, they should clear all subjects.</li>
                        <li>The entrance test is a mandatory examination for any student who wishes to apply for the course.</li>
                        <li>In the entry test, candidates should earn minimum marks.</li>
                        <li>For reserved category students, they will have to display their reservation certificates issued by qualified authorities.</li>
                        <h2>Diploma in Library and Information Science (DLIS): What is it About?</h2>
                        <p>This course is designed to prepare the student to work in a library in a school or college , in government or private sector. There are other options available as a consultant or research assistant.</p>
                        <li>Cataloguing, organising and storing of documents or books or similar resources is taught.</li>
                        <li>This course lays emphasis on developing skills like communication skills, problem solving skills, patience, social skills and the skill of performing under pressure. </li>
                        <li>Also someone with a good memory will be preferred more as one is dealing with such an enormous amount of data. </li>
                        <li>Although there are various computer applications available which ease the work of the librarian to a great extent, yet a good memory power goes a long way in keeping track of the changes made, example books ordered in a particular genre of a particular writer when dealing with literature students.</li>
                        <li>Knowledge of computers is given so that the librarian can successfully use the applications designed for making library management easier.</li>
                        <h2>Why Study Diploma in Library and Information Science (DLIS)?</h2>
                        <li>This is one sector where there isn't much competition if one is after getting a lucrative government job with all its perks.</li>
                        <li>The diploma is only of one year as opposed to 3 year degrees and can be done conveniently from home via distance education programme.</li>
                        <li>The students can go for higher education after doing diploma and then try to get better packages both in private firms and government jobs.</li>
                        <li>The fresher salary is good, and after a few years one can expect getting much better salaries as experience will be counted. Along with that one can pursue his higher degree via correspondence (distance education).</li>
                        <li>There is a huge variety of jobs, documentation officer, consultant or working as a librarian in schools or colleges. If you want the stability of a single place, doing a routine job then being a librarian is good for you.</li>
                        <li>Someone who is interested in reading and writing can easily get into a reputed publishing industry and it might just help them build the contacts they need for getting their work published.</li>
                        
