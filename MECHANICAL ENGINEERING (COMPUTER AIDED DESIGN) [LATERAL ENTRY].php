                        <h2>What is Computer Aided Design</h2>    
                        <p>Computer-aided Design (CAD) Course is the process of creation, modification, analysis and optimisation of designs with the help of a computer or workstation is termed computer-aided design (CAD). There are many software solutions that are used for this purpose; they are called CAD software. The productivity of a designer has been enhanced manifold by the use of this Computer-aided Design (CAD) software. They are also used for improving the quality of design and communication. The database for manufacturing is created using CAD software.</p>
                        <p>When both drafting and designing are done with the help of a computer, the process is called computer-aided design and drafting or simply CADD. In the case of its utilization in electronic systems designing, it is known as Electronic Design Automation (EDA) and when used for mechanical design creation, it is termed Mechanical Design Automation (MDA). These are nothing but different methods of creating technical drawings for different branches of engineering with the use of computer software.</p> 
