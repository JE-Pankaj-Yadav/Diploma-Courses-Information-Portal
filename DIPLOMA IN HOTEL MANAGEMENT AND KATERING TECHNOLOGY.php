                        <h2>SOMTHING ABOUT DIPLOMA IN HOTEL MANAGEMENT AND KATERING TECHNOLOGY</h2>
                        <p>Diploma in Hotel Management is a 3-year diploma level Hotel Management course. Divided into six semesters, diploma in hotel management course focuses on the management of hotel administration, accounts, advertising, housekeeping, front office or front office, food and beverage management, catering, and upkeep.</p>
                        <p>The Institute of Hotel Management, Catering Technology and Applied Nutrition (IHMCT&AN), generally known as Institute of Hotel Management (IHM), is a multi-location public hospitality school in India. IHM is established by the Ministry of Tourism of the Government of India. It is also under the jurisdiction of Ministry of Tourism. It caters to not only the students of India but also international students from the SAARC countries.</p>
                        <h2>Hotel management structure</h2>
                        <p>The size and complexity of a hotel management organizational structure varies significantly depending on the size, features, and function of the hotel or resort. A small hotel operation normally may consist of a small core management team consisting of a Hotel Manager and a few key department supervisors who directly handle day-to-day operations. On the other hand, a large full-service hotel or resort complex often operates more similarly to a large corporation with an executive board headed by the General Manager and consisting of key directors serving as heads of individual hotel departments. Each department at the large hotel or resort complex may normally consist of subordinate line-level managers and supervisors who handle day-to-day operations.</p>
