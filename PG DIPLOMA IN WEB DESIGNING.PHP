                        <h2>Advantages of Diploma in Web Designing</h2>
                        <li>Web Designing is a specialisation in IT firms which is designed to make candidates eligible to make and maintain websites.</li>
                        <li>Web designing has a big scope in today's era, it includes web graphic design, user experience design, user interface design, site architecture, search engine optimization, Website UI Design, etc.</li>
                        <H2>Tools and technologies.</H2>
                        <P>Web designers use a variety of different tools depending on what part of the production process they are involved in. These tools are updated over time by newer standards and software but the principles behind them remain the same. Web designers use both vector and raster graphics editors to create web-formatted imagery or design prototypes. Technologies used to create websites include W3C standards like HTML and CSS, which can be hand-coded or generated by WYSIWYG editing software. Other tools web designers might use include mark up validators[8] and other testing tools for usability and accessibility to ensure their websites meet web accessibility guidelines.</P>
                        <H2>Marketing and communication design.</H2>
                        <P>Marketing and communication design on a website may identify what works for its target market. This can be an age group or particular strand of culture; thus the designer may understand the trends of its audience. Designers may also understand the type of website they are designing, meaning, for example, that (B2B) business-to-business website design considerations might differ greatly from a consumer targeted website such as a retail or entertainment website. Careful consideration might be made to ensure that the aesthetics or overall design of a site do not clash with the clarity and accuracy of the content or the ease of web navigation,[10] especially on a B2B website. Designers may also consider the reputation of the owner or business the site is representing to make sure they are portrayed favourably.</P>
