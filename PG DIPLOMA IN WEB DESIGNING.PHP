                        <h2>SOMTHING ABOUT PG DIPLOMA IN WEB DESIGNING</h2>
                        <p>Diploma in Web Designing is a 1 year long course depending on the university where candidates learn about various aspects of designing a website. Web Designing is a skilled field in IT sectors. The diploma is designed in such a manner to offer sufficient knowledge and expertise to work on different software.</p>
                        <h2>Web Designing Courses</h2>
                        <p>Web designing courses requires the designers to create graphics, typography, and images that are only used on the World Wide Web commonly known as internet. While creating a design, web designers must maintain a balance between creating a good design and the speed. Web design is about designing websites for desktop browsers; however, since the mid-2010s, design for mobile browsers and tablets has become increasingly important. </p>
                        <p>Web Designing Courses can be studied at Diploma, Certification, PG Diploma, etc. levels of study. Certificate courses in web designing, are some of the topmost courses in Web Designing courses that students can pursue in online as well as offline mode of study. </p>
                        <p>Also, thereâ€™s a great demand for Free Online Web Designing Courses, Web Designing Courses for Beginners, which students can find on Udemy Web Design Courses. The Web Designing Courses usually take 1 month to 2 years for completion depending upon the type or level of the Web Designing Course and the Web Designing Course Fees ranges between INR 5,000 to INR 3,00,000 in India.</p>
                        <p>The different areas a Web designer can work in include web graphic design, interface design, creative, user experience design, and a lot more. According to a recent salary survey data by PayScale, the average salary offered to a Web Designer in India is INR 2,83,036 per annum. </p>
                        <h2>All About Web Designing</h2>
                        <li>A web designer works on the look, layout, and in some cases the content of a website. The three main categories of Web Design, namely, Responsive web design (RWD), Landing page design and Google Web Design are discussed below:</li>
                        <li><b>Responsive web design (RWD)</b> is a web development approach that creates dynamic changes in the appearance of a website, depending on the size of the screen and the orientation of the device used to view it . The multitude of devices available to customers, ranging from small phones to huge desktop monitors. </li>
                        <li>RWD uses what are called breakpoints to determine what a site's layout will look like: one design is used above a breakpoint and another design is applied below. below this breakpoint. Breakpoints are generally based on the width of the browser.</li>
                        <li><b>Landing page design</b> is the method of making a great website page for a designer's audience and website visitors. It ought to encourage them to convert from leads into subscribers or customers. </li>
                        <li>Effective landing page design is on-brand, includes designerâ€™s product or service and company information, and incorporates relevant offers and calls-to-action (CTAs).</li>
                        <li><b>Google Web Design</b> offers a free suite of features for creating interactive, responsive, and animated ad creatives. The tool can be used to create advertisements and banners that work on all mobile and desktop devices. </li>
                        <li>One can add various components to their ads, such as iFrames, maps, image galleries, YouTube videos, and touch zone features. One can customize their content to react to actions such as shake and rotate on mobile devices.</li>
                        <h2>Web Designing Course Eligibility</h2>
                        <li>The general eligibility criteria of a Web Designing course at Certification, Diploma and Undergraduate level is that a student must have completed their 10+2 from a recognised board with at least 50% marks.</li>
                        <li>And, the eligibility criteria of a Web Designing course at Postgraduate level is that a candidate must hold a bachelor degree in Web Designing or other relevant discipline from a recognized university.</li>
