                        <h2>About Diploma in Mechanical Engineering Course</h2>
                            
                        <p>The increased drive for automation in the operation process of industries and the need for newer variants of machines has led to the increased demand in technicians and mechanical supervisors around the world. The Mechanical Diploma Holders are generally engaged in supervisory categories in different industries . The diploma in Mechanical Engineering Syllabus is designed in such a way so that the students are able to handle different technical issues in Mechanics accordingly.</p>
                            
                        <h2>What is Diploma in Mechanical Engineering?</h2>
                            
                        <ul>
                        	<li>Diploma in Mechanical Engineering is about the study of Mechanical parts and devices.</li>
                        	<li>After completing the course students will be able to handle, repair, and design the Vehicle Engines, Engine or Watercraft, and Aircraft.</li>
                        	<li>The course teaches students the concept of industrial equipment machinery, kinetics, fluid mechanics, and Robotics.</li>
                        </ul>
                            
                        <h2>Who should Study Diploma in Mechanical Engineering?</h2>
                            
                        <ul>
                        	<li>Candidates who have interest in studying engineering and its related field should take up the course.</li>
                        	<li>Candidates who want to pursue a career as a Hardware engineer should take up the course.</li>
                        	<li>Students who wish to build upon their knowledge of different topics of mathematics and physics such as materials, solid mechanics, thermodynamics, product design, etc in higher secondary level should take up the course.</li>
                        	<li>Candidates who want to pursue career as a industrial engineer can take up the course.</li>
                        	<li>Students who have a career planned in robotics, automobile manufacturing, aerospace engineering should take up the course.</li>
                        	<li>Ethical and passionate candidates who have planned to start their own business should take up the course.</li>
                        	<li>This course is also effective for candidates who wish to gain a certificate in mechanical engineering in a comparative shorter period of time, and be job ready.</li>
                        </ul>
                            
                        <h2>Why study Diploma in Mechanical Engineering?</h2>
                            
                        <ul>
                        	<li>Students can join B. Tech direct from the second year after completion of the course</li>
                        	<li>Students get job easily in the government and Private sector</li>
                        	<li>Students learn Designing and Maintaining mechanical parts</li>
                        	<li>The students also have a chance to join the aviation industry as a technician in the Airlines.</li>
                        	<li>Students become trained and have the skills to start their own initiative as Mechanics and start their own research lab.</li>
                        </ul>
                            
                        <h2>Diploma in Mechanical Engineering: Types</h2>
                            
                        <p>Due to the growing demand of automation and skilled engineers, different types of mechanical courses have been introduced keeping in mind the demand of various students. There are mainly two types of courses available one is Full time Diploma in Mechanical Engineering (3 years) and other is part time diploma in Mechanical engineering. Apart from these Distance Diploma In Mechanical Engineering is available from IGNOU. See: <a href="https://collegedunia.com/courses/mechanical-engineering/certification-courses-for-mechanical-engineers" target="_blank">Certification courses for Mechanical engineers</a>&nbsp;</p>
                        
                        <h2>Full-Time Diploma in Mechanical Engineering</h2>
                        
                        <ul>
                        	<li>A full time diploma in Mechanical engineering is a 3 years professional course offered by various government and private institutions.</li>
                        	<li>The average course fee ranges between INR 10,000-INR 2,00,000.</li>
                        	<li>The admission in the course is done through the entrance examination such as Odisha DET, MP DET, JEXPO, etc.</li>
                        	<li>The top colleges offering mechanical engineering diplomas are ACP Polytechnic Kolkata, JMI University, Lovely professional university and many more.</li>
                        </ul>
                        
                        <h2>Part Time Diploma in Mechanical Engineering</h2>
                        
                        <ul>
                        	<li>The duration of the part time diploma in Mechanical Engineering is 4 years.</li>
                        	<li>The courses are approved by the AICTE and the concerned state board or agencies.</li>
                        	<li>The minimum eligibility criteria is that the candidates must pass their 10th or equivalent examination with at least 50% marks. In some cases the marks can be as low as 35%.&nbsp;</li>
                        	<li>The course is provided to people who are working and want to improve their skills in the mechanical domain.</li>
                        	<li>Part time Diploma in Mechanical Engineering is provided by the following colleges, National Institute of Business Studies, CIT Sandwich Polytechnic, Hooghly Institute of Technology, and many more.</li>
                        	<li>The average fee is around INR 35,000-INR 50,000.</li>
                        </ul>
                        
                        <h2>1 year Diploma in Mechanical Engineering</h2>
                        
                        <p>One year Diploma in Mechanical Engineering is a correspondence course pursued by students who cannot attend regular classes. All the materials and projects are given to you either on the internet or through a post. After doing the course many of the students get direct entry into BTech Mechanical or BE Mechanical.</p>
                        
                        <h2>Eligibility</h2>
                        
                        <p>Students looking for admission into a course of Engineering Diploma in One Year must have passed the Secondary School (10th) examination conducted by a Recognized Board or any other examination recognized as equivalent thereto, as per the standard / eligibility criteria laid down by the Institute/University.</p>
                        <h2>Distance Diploma in Mechanical Engineering</h2>
                        
                        <p>Distance Diploma in Mechanical engineering is generally a 3 years course that is pursued by working professionals.</p>
                        
                        <h2>Distance Diploma in Mechanical Engineering: Admission 2022</h2>
                        
                        <p>In the case of Distance Diploma in Mechanical engineering, the institutes do not ask for entrance exams. The admission is provided on the basis of the marks obtained in the 10th level board exam. The details of different diploma in Mechanical engineering program is provided in in following section</p>
                        
                        <h2>IGNOU Diploma in Mechanical Engineering</h2>
                        
                        <ul>
                        	<li>IGNOU's Distance Diploma in Mechanical Engineering Course is offered to persons who have passed their 10th exam with 55% marks, or passed 10th with ITI.</li>
                        	<li>Candidates that have passed 12th with physics, chemistry and maths are eligible.</li>
                        	<li>Candidates who have relevant experience in manufacturing industries are eligible.</li>
                        	<li>The minimum duration of this course is 3 years and the maximum duration is 6 years.</li>
                        	<li>The course fee is INR 12,500</li>
                        	<li>There is no age limit for this course.</li>
                        </ul>
                        
                        <h2>IIME Diploma in Mechanical Engineering</h2>
                        
                        <ul>
                        	<li>The Duration of IIME Distance Diploma in Mechanical engineering is for three years.</li>
                        	<li>The eligibility criteria is passing either the 10th or 12th exam.</li>
                        	<li>The 10th pass candidate must have work experience of 3 years and the 12th pass candidates must have experience of 1 year.</li>
                        	<li>The course fee is INR 60,600.</li>
                        </ul>
                        
                        <h2>Neptune Institute of Management and technology Diploma in Mechanical Engineering</h2>
                        
                        <ul>
                        	<li>The duration of the course is 3 years.</li>
                        	<li>In order to qualify for this course the candidates must pass their 10th or equivalent exam.</li>
                        	<li>The candidates must have a work experience of 2 years.</li>
                        	<li>The course fee is INR 8600</li>
                        </ul>
                        <h2>Diploma in Mechanical Engineering: Admission</h2>

                        <p>The admission to the Diploma in Mechanical Engineering is done mainly on the basis of the Entrance Exams conducted by the different states.</p>

                        <h2>Diploma in Mechanical Engineering: Eligibility Criteria</h2>

                        <ul>
                        	<li>Candidates must be 10th pass with a minimum of 55% marks with Mathematics and English as the mainstream subjects.</li>
                        	<li>In some states the minimum marks required for the 10thor equivalent exam is 35%.</li>
                        	<li>Candidates studying vocational or ITI courses are eligible to get admission in the second year of the course.</li>
                        	<li>Candidates have to pass the different state entrance exam to get admission.</li>
                        </ul>

                        <h2>Diploma in Mechanical Engineering: Admission 2022</h2>

                        <p>As an aspiring candidate for the Diploma in Mechanical Engineering, you can apply for the course in a government or private college.</p>

                        <ul>
                        	<li>Doing the course from a government college is a plus point for your career as they have comparatively better pavement record and lower admission fees.</li>
                        	<li>Every Indian state conducts a State-Level Engineering Diploma Entrance Exam.</li>
                        	<li>For private colleges, you can get direct admission based on marks on the 10th. Some private colleges conduct their own entrance exam.</li>
                        </ul>

                        <p>After getting sufficient marks in the entrance exam, you should following the standard admission process for Diploma in Mechanical Engineering -</p>

                        <ul>
                        	<li>Visit the official website of the college you want to apply for and fill out an online application form.</li>
                        	<li>The admission department of the college will examine the application and make a list of selected candidates for counselling.</li>
                        	<li>After getting your name on the list you should present for the counselling process on a given date.</li>
                        </ul>
