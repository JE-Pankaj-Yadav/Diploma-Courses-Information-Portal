                        <h2>SOMTHING ABOUT MECHANICAL ENGINEERING</h2>
                        <p>Diploma in Mechanical Engineering is a 3 year polytechnic course that can be pursued by students after class 10. Diploma in Mechanical Engineering deals with design, manufacture and maintenance of mechanical equipment by applying the basic principles of mechanics. </p>
                        <p>Mechanical Diploma or Mechanical Engineering polytechnic courses are offered to the candidates immediately after their 10th or equivalent exam. The eligibility for Mechanical diploma courses are a minimum score of 55% marks in class 10. A relaxation of 5% is provided to the candidates belonging to the reserved categories.</p>
                        <p>Admission to the Diploma in Mechanical Engineering Courses is done on the basis of entrance examinations conducted by the various state examination board.</p>
                        <p>The diploma in Mechanical Engineering syllabus consists of the different subjects such as Fluid Mechanics and Machinery, Manufacturing Technology-I, Theory of Machines, Fluid Power Engineering, etc.</p>
                        <p>After Diploma in Mechanical Engineering students can get admission in 2nd year of BTech course through BTech Lateral Entry. Most engineering students go for MBA after graduation so that they can get a managerial career and earn a high salary. Students need a BTech degree in order to study MTech.</p>
                        <p>Top Diploma in Mechanical Engineering Colleges in India includes Jamia Milia University, Bharati Vidyapeeth Deemed University, CV Raman Global University etc The average course fees range from INR 10,000 to INR 2 lacs.</p>
                        <h2>Diploma in Mechanical Engineering: Course Details</h2>

                        <ul>
                        	<li>The duration of the Diploma in Mechanical Engineering is 3 years.</li>
                        	<li>The candidates can pursue Diploma in Mechanical Engineering immediately after their 10th entrance exam.</li>
                        	<li>The entry to Mechanical engineering polytechnic course is either based on merit list or entrance exam.</li>
                        	<li>The top entrance exams for Diploma in Mechanical engineering are JEECUP, JEXPO, Odisha DET, etc.</li>
                        	<li>The top recruiters for the Mechanical engineering Diploma courses are Bajaj, Ford, Honda, NTPC, BHEL Hindustan Unilever, LT, Ashok Leyland, etc.</li>
                        	<li>Candidates who have high aptitude and interest in machines, advanced mechanics, robotics should pursue this course.</li>
                        	<li>Candidates after completion of the course are accepted in different job profiles such as Mechanical Engineer, Manufacturing Manager, Sanction Head, Sales Engineer, Mechanical Technician, etc.</li>
                        	<li>The average salary of Mechanical Diploma Holders range between INR 20000-INR 500000.</li>
                        </ul>
                            
