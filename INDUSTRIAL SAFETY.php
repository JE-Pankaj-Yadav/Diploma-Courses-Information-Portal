                        <h2>SOMTHING ABOUT INDUSTRIAL SAFETY</h2>
                        <P>Safety stands as the foremost priority in most of today’s technology-aided industrial processes that deal with large amounts of energy. A well-engineered safety system is critical for the protection of workers, equipment, the environment & society. And this is what Industrial Safety is all about! It is a countermeasure used to protect humans and assets against any risks, hazards, near misses and accidents. </P>
                        <p>An industrial safety system is a countermeasure crucial in any hazardous plants such as oil and gas plants and nuclear plants. They are used to protect human, industrial plant, and the environment in case of the process going beyond the allowed control margins.</p>
                        <p>As the name suggests, these systems are not intended for controlling the process itself but rather protection. Process control is performed by means of process control systems (PCS) and is interlocked by the safety systems so that immediate actions are taken should the process control systems fail.</p>
                        <p>Process control and safety systems are usually merged under one system, called an integrated control and safety system (ICSS). Industrial safety systems typically use dedicated systems that are SIL 2 certified at minimum; whereas control systems can start with SIL 1. SIL applies to both hardware and software requirements such as cards, processors redundancy and voting functions.</p>

                    </div>
                    <h4>What is Industrial Safety?</h4>
                    <p>The term “Industrial safety” is a set of safety protocols, policies and regulations to protect the workers, workplace, work equipment and environment from hazards. It acts as a corrective measure that is reviewed and approved by safety experts periodically to ensure a safe workplace and sustainable business operations. </p><br>
                    <h4>Why Industrial Safety is necessary?</h4>
                    <p>With the alarming rate of industrial accidents happening every year, industrial safety is a crucial step save human lives. Moreover, workers must be educated on how to handle the equipment and basic safety procedures for adequate safety.</p>
                    <p>Let’s look at some main reasons why industrial safety is important,</p><br>
                    <h4>Moral responsibility</h4>
                    <p>It is the duty of care of an employer to affirm the basic morale of a workplace as it costs lives, longer recovery time, lost productivity and heavy personal inconvenience when injured.  It is also the responsibility of an employer to be ethical and create a safe working environment.</p><br>
                    <h4>Legal responsibility</h4>
                    <p>ILO standards on Occupational Health & Safety proffers essential tools for employers, workers, and government in order to provide maximum safety at work. International Labour Organization (ILO) defines the global standards of protecting human rights that include working duration, employee welfare, safe practices, wages & compensation, labour requirements and other safety aspects which is practised internationally.</p>
                    <p>The Factories Act 1948 (amended on 1987) formulated national policies in India specifically for occupational safety health in factories & docks in India. It deals with the health, safety, efficiency & well-being of workers at the workplace.</p><br>
                    <h4>Financial Responsibility</h4>
                    <p>It is a legal requirement for employers to have insurance and also to plan for uninsured costs with their consequences. This will reduce the financial liability towards loss of property, equipment and productivity.</p><br>
                    <h4>Objectives of Industrial Safety</h4>
                    <p>In any incident, industrial safety is the first discussion that is to be verified again to avoid future accidents. Every hazardous work has its own unique set of safety regulations but it can be distilled into a set of objectives that will assure the safety of any industry. The objectives are</p>
                    <li>To achieve a safe and accident-free workplace</li>
                    <li>To reduce the business’s cost to accident compensation and insurance claims related to workplace accidents</li>
                    <li>To ensure a high-morale work environment for a safer and smoother productivity</li>
                    <li>To have competent supervision on the workers to maximize protection</li><br>
                    <h4>Important considerations for Industrial Safety</h4>
                    <p>Some of the important considerations for Industrial safety are listed below:</p>
                    <li>Health & Safety Management Systems</li>
                    <li>Managing Risk</li>
                    <li>Risk Assessment</li>
                    <li>Monitoring & measuring Health & Safety</li>
                    <li>Manage workplace health & safety</li><br>
                    <h4>Work activities that may lead to Accidents</h4>
                    <li>Fire & Electricity</li>
                    <li>Equipment & machinery</li>
                    <li>Work at height, confined space, lone working</li>
                    <li>Hazardous substances</li>
                    <li>Manual handling, load handling equipment</li>
                    <li>Movement of people and vehicles in workplace</li>
                    <li>Mental-ill health, substance abuse, violence at work</li>
                    <li>Poor work environment</li><br>
                    <h4>Scope of Industrial Safety Training for Workers</h4>
                    <p>Safety training educates workers and management staff to identify safety hazards and apply the proven safety methods. It is crucial for workers to learn technical and practical knowledge to avoid loss of lives, injuries and health hazards. </p>
