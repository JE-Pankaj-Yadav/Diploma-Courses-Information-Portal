						<h2>Required Skillset for Textile Chemistry</h2>
						<p><b>Skillsets Required for Textile Chemistry Course</b></p>
                        <p>The skills required for different job profiles vary vastly from one organisation to another. However, these skills can be broadly categorised into three umbrella terms given as follows:</p>
                        <ol>
                            <li><b>Technical skills:</b> Candidates who graduate in B.Tech textile chemistry are required to have technical skills as they assist in fulfilling intricate tasks and processing them smoothly.</li>
                            <li><b>Theoretical Base:</b> Scientific knowledge about chemistry and its fundamentals are very significant because of the need for practical application. Candidates must have a strong base of theoretical knowledge so that they can associate with the problems and interpret them according to the predetermined principles studied.</li>
                            <li><b>Analytical skills:</b> Textile chemistry involves drawing inferences, characterising properties into segments, and analysing the test conducted to dig results. Hence analytical skills are an integral part of textile chemistry.</li>
                        </ol>
						<h2>Course Curriculum for Textile Chemistry</h2>
						<p><b>Textile Chemistry- Course Curriculum</b></p>
                        <p>The division of Curriculum is an integration of a set of subjects distributed evenly into a specified number of semesters. The curriculum varies according to the type of degree pursued in textile chemistry.</p>
                        <p>The Master of Science in textile chemistry degree goes up to 4 semesters whereas the Bachelor of Technology in textile chemistry goes up to 8 semesters.</p>
                        <p>MSc in textile chemistry constitutes more theoretical knowledge based on principles, reactions, and laboratory applications whereas a B.Tech degree in textile chemistry entails more practical based knowledge, production, and composition. More or less, the subjects remain the same, but the qualifications differ slightly.</p>
									
