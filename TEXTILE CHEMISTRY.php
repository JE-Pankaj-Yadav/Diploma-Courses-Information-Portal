                        <h2>SOMTHING ABOUT TEXTILE CHEMISTRY</h2>
                        <h2>What is Textile Chemistry?</h2>
                        <p>Clothing is one of the basic requirements of life. This requirement cannot be substituted and hence contains a multitude within itself. Textile chemistry in the simplest of terms is a branch of applied chemistry that studies all about textiles and fabric, from their occurrence to their utilisation. Textile Chemistry uses the fundamentals of chemistry as the basis for studying and digging into the manufacturing of textiles as well as the technical compositions of the smallest unit of textiles. This scientific study, Textile Chemistry has a huge scope in itself as it is limited regarding one domain but still advanced within that very domain. Pursuing a career in textile chemistry would mean staying updated with the changes taking place over the years in yarns, fibres, weaves, compositions, dying, quality checking, storage, etc. Apart from mainstream Fashion Technology, this course has gained popularity in recent years because of its curriculum. A lot of renowned universities have been providing degrees in textile chemistry.</p>
                        <p>A lot of people at times incline to enter the fashion industry but do not have an interest in conventional ways, including designing. They, however, have a knack for production and keeping, this is the most suitable course in that regard. The textile Chemistry course is taught in top-level Universities, private and public both, with placement opportunities in renowned fashion brands.</p>
                        <h2>Eligibility Criteria (UG & PG) of Textile Chemistry</h2>
						<p><b>Eligibility Criteria for Textile Chemistry Course</b></p>
                        <p>Aspirants who wish to pursue the textile chemistry course must meet the mentioned eligibility criteria: </p>
                        <p><b>For Undergraduate Course in Textile Chemistry</b></p>
                        <ol>
                            <li>Must have passed class 12th from a recognised board with subjects like physics and chemistry.</li>
                            <li>Must have minimum aggregate marks-40-50% to be eligible for textile chemistry course.</li>
                            <li>Undergraduate Degrees for Textile Chemistry courses majorly include Bachelors of Technology and Bachelor of Science.</li>
                        </ol>
                        <h2>Eligibility criteria for a postgraduate Textile Chemistry</h2>
                        <ol>
                            <li>Candidates should have scored at least 50% aggregate in the bachelor's degree.</li>
                            <li>The candidate must have completed an undergraduate degree in Bachelor of Science or Bachelor of Technology in Textile Chemistry from recognised institutions.</li>
                            <li>To secure admission in the Master's program, candidates must have cleared the entrance exam conducted by their respective universities.</li>
                            <li>After the candidate clears the entrance exam, a personal interview and group discussion are conducted. Afterward, a provisional list is released which contains the names of all the selected candidates.</li>
                        </ol>
