                        <h2>SOMTHING ABOUT PG DIPLOMA IN COMPUTER APPLICATION</h2>
                        <p>Computer Applications play a very important role in today's scenario as every individual is significantly dependent on machines. A computer is one such machine, which has made its presence in every walk of our life. With the wide range of applications, it has to offer, the need for learning computer science is now more than ever. The recent boom in the IT Industry tells us that the experts in the field of computer applications are needed both in India and abroad. Moreover, with the government's ambitious plan of digitalization, it may go a notch higher. The huge gap in demand and supply in the field of computer applications help them command a better salary.</p>
                        <p>There's a slight difference between Computer science and Computer applications which people usually tend to ignore. Computer Science is a detailed and systematic study of the fundamentals of working on a computer, whereas computer applications are the study of the application of computer software. In short, while the former is a study of how computers work, the latter helps us understand all sorts of work that can be done on a computer.</p>
                        <P>Application (MCA) is a postgraduate degree in computer application awarded in India. It is a three year (6 semester) for all eligible bachelor degree holders and two year (4 semester) course for candidates having Bachelor of Computer Application (BCA) or B.Sc (IT) degree. The students entering MCA must have a bachelor degree with Mathematics as one of the subjects at higher secondary or graduation. [1]The program is a blend of both theoretical and practical knowledge.</P>
                        <h2>Eligibility Criteria (UG & PG) of Computer Applications</h2>
                        <p>The eligibility criteria can vary from college to college. But, here is a broad overview of minimum requirements for the various courses.</p>
                        <h2>For Diploma In Computer Applications</h2>
                        <li>No entrance test is required to get a Diploma in Computer Applications.</li>
                        <li>Candidates are selected on the basis of their marks in class 12th.</li>
                        <h2>For Undergraduate Computer Application Courses </h2>
                        <li>Candidates must have scored an aggregate of minimum 50-55% in 10+2.</li>
                        <li>Aspirants can hail from any stream with English as one of their subjects in Class 12. However, some colleges mandatorily require having studied science subjects in class 12th.</li>
                        <h2>For Postgraduate Computer Application Courses</h2>
                        <li>Candidates must have a graduation in Computer applications, Computer science or any other related field with a minimum of 50% marks or a GPA of 5/10 at the undergraduate level.</li>
                        <li>Most colleges require Mathematics as one of the compulsory subjects at 10+2 or graduation level.</li>
                        <li>A minimum of 50-55% in Class 12.</li>
                        <h2>For PhD in Computer Applications</h2>
                        <li>Candidates must have completed their Masters in Computer applications, computer science or any other related field without any pending backlog with 50-55%. </li>
                        <h2>Scope of Computer Applications in India and Abroad</h2>
                        <p>BCA & MCA Courses open the door of opportunities for the degree holders. The students have wide-ranging employment opportunities in various sectors like Banking, Networking, Insurance, e-commerce, IT Industry, MNCs, Marketing industry, Accounting etc. Most of the MNCs even send the candidates onsite to some different location abroad, which provides them with better exposure. Some leading IT companies even offer up to 8 L to new joiners.  Apart from top IT firms, Startups and Consulting firms also pay a decent salary.</p>
                        <p>There is also a plethora of lucrative opportunities in the public sector or government jobs. Indian Army (Air force and naval force as well) also employs many IT professionals. Some students even go for MBA after completing their BCA, which helps them land a high paying job in the IT Industry. International offers are also common after MBA from top B-School.</p>
                        <h2>Careers in Computer Applications</h2>
                        <p>Computers have found their way from small shops to giant multinational companies. And so has the demand for the skilled IT Professionals. Fresh graduates from Computer applications are working as Graphics Designer, Digital Marketer, Software developer, Technical writer etc. The companies usually prefer candidates with a master's degree to candidates with a diploma or bachelor's degree. That is why many students go further for a postgraduate degree. The ROI for most of the computer application courses fare really well than their counterparts.</p>
                        <h2>Average Salary</h2>
                        <p>The contribution of the software industry to the nation's GDP has taken a huge leap from 1.2 (in 1997) to almost 7.7 (in 2017) in 30 years. This can easily be reflected in the ever-rising demand for IT professionals at entry-level. On a closer look at the compensation distribution amongst various job profiles, we observe that Analysts bag the highest salary (approximately Rs. 5.5- Rs. 6.5 lakh). This is followed by a Software developer or App developer who earn close to Rs. 5 Lakh. These figures are just a rough estimate to get an overview. A lot depends upon the calibre and hard work of individuals. The salary increases exponentially with an increase in experience.</p>
                        <h2>Course Curriculum for Computer Applications</h2>
                        <p>The curriculum for the Computer applications course is designed to equip the students with basic knowledge of computers and its system. It grooms an individual to be able to face the challenges in the professional world. The course is meticulously structured from basics to advanced level in a span of three years. The course does not require the individuals to be well versed in programming languages, but a prior knowledge can prove to be a plus point. Students pursuing their MCA choose their specialization from the options available in the college.</p>
                        <p>In addition, the fees for the course is not very high, making it affordable for students from middle-class families as well. Private colleges usually charge higher than government colleges. The annual fees for MCA in JNU is as low as Rs. 1750 but the competition makes it very difficult to gain admission.</p>
