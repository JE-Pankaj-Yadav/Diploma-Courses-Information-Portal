                        <h2>SOMTHING ABOUT CHEMICAL ENGINEERING (PETRO CHEMICAL)</h2>
                        <p>Diploma in Petrochemical is a 3-year program founded on the principles relating chemistry, physics and mathematics. Petrochemical Engineering finds application in extracting oil and natural gas from the ground thereafter converting these resources into fuels and finished products. It further deals with the chemical processes involved in turning the crude oil and petroleum into useful products such as food, clothes, fertilizers and plastics. Also, the petroleum industry is developing every single day and it needs efficient professionals who can change the entire dimension of this field, putting forward tremendous efforts.</p>
                        <p>The students are provided practical experience by on-field internships, industrial visits and industry defined projects. Few of the industries related to the program are Reliance Industries Limited, Deepak Phenolics Ltd., ONGC Ltd., Gujarat State Petroleum Corporation Limited, Supreme Petrochem Ltd, Haldia Petrochemicals Limited and IOCL Ltd., Hindustan Petrochemicals Ltd., NOCIL Ltd. and Bharat Petrochemicals Ltd. Also there is a good opportunity in the Middle East with good annual income packages. Rather than stepping into the industry, on completion of the Diploma course, students can opt for a Bachelor's Degree in Petroleum Engineering.</p>
                        <h2>Eligibility</h2>
                        <p>Passed 10th Std./ SSC examination from a Recognized Board with minimum 35% Marks</p>
