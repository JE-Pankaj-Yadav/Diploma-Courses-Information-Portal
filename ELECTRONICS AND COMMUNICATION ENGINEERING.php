                        <h2>Why should you pursue a Diploma in Electronics and Communication Engineering?</h2>
                        
                        <p>The goal to study Diploma in Electronics and Communication Engineering depends upon individual aspirations. Some of the few reasons to pursue this course is mentioned below:</p>
                        
                        <ul>
                        	<li>The Electronics and Communication branch provides freedom. The freedom to drift between hardware field and software field.</li>
                        	<li>It trains students in areas such as-Electronic circuits, Electrical systems, Communication Engineering, Signal systems, Signal processing, VLSI, Software etc.</li>
                        	<li>The key skills developed during the course are critical thinking, aptitude for maths, communication skills, strong IT skills, time management, complex problem solving skills, etc.</li>
