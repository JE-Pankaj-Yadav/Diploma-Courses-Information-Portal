                        <h2>Diploma in Electronics and Communication Engineering Admission Process</h2>
                        <p>Admissions in Diploma courses are also done through both merit and entrance exams.</p>
                        
                        <h2>Direct Admission</h2>
                        
                        <ul>
                        	<li>Candidates who are willing to get admission in this stream can apply to Diploma in Electronics and Communication Engineering colleges by both offline and online means.</li>
                        	<li>You can either reach the official website of the institute or to this step admission office of the institute to fill the admission application form.</li>
                        	<li>Fill the application form required and upload all the necessary documents which are asked in the form.</li>
                        	<li>After completion of the process of application, go for counselling round and attend the personal interview round to get your desired college.</li>
                        </ul>
                        
                        <h2>Diploma in Electronics and Communication Engineering: Eligibility Criteria</h2>
                        
                        <ul>
                        	<li>Candidates must have cleared the equivalent exam for their desired college.</li>
                        	<li>Appearing Candidates must have passed 12th from Science Stream having Maths, Chemistry and Physics as main subjects from any recognized board.</li>
                        </ul>
                        
                        <h2>Entrance Exam Based Admission</h2>
                        <p>The student must fulfill the minimum eligibility criteria to apply for Diploma in Electronics and Communication Engineering. The admission depends upon the minimum educational qualification which students will have to fulfill for the course.</p>
                        <p>Colleges offering Diploma in Electronics and Communication Engineering course release application forms at different times. Students need to follow the steps given below to apply for a Diploma in Electronics and Communication Engineering.</p>
                        
                        <ul dir="ltr">
                        	<li><b>Registration-</b> the registration date will be released by the institute. Students will have to prepare accounts with general details like phone number, email id, etc.</li>
                        	<li><b>Fill up details-</b> once the application form releases students will have to fill all the details in it carefully.</li>
                        	<li><b>Attach the documents-</b> Students will have to scan all the necessary documents which have been asked by the institute to attach with the application forms. Documents like mark sheets, caste certificates, etc will be needed.</li>
                        	<li><b>Application fee-</b> After filling up the application forms completely students will have to pay the application fee and submit the form finally.</li>
                        	<li><b>Download the admit card-</b> After some days the admit card will release. Students will have to download it from the official website of the institute and check the date of the examination given on the admit card. The admit card will have the day and date of the examination written over it. Students will have to carry it to the examination center on the day of the examination.</li>
                        	<li><b>Examination-</b> Students will have to appear for the examination on the date mentioned on the admit card.</li>
                        	<li><b>Results-</b> Results will be released after some days of the examination. Students will have to appear for the counseling and admission process if he/she is successful in the examination.</li>
                        	<li><b>Counseling and admission-</b> Those students who have passed the entrance exam will have to appear for the counseling process to take admission. The student can now take admission in the Diploma in Electronics and Communication Engineering course.</li>
                        </ul>
                        <h2>Diploma in Electronics and Communication Engineering: Entrance Exam Preparation Tips</h2>
                        
                        <ul>
                        	<li><b>Divide your schedule:</b> Studying without a deadline isn't something that candidates can afford, therefore they must assign their days to certain topics and finish them within the deadline.</li>
                        	<li><b>Keep your resources limited:</b> It is a must for candidates to remain focused and having too many resources will only alleviate the stress. Therefore, they must research well and shortlist some resources for the study plan and stick to it. Remember, Candidates can't cover everything from everywhere, therefore keep it simple and accurate.</li>
                        	<li><b>Revision is the Key:</b> Candidates should not keep revision for the last minute. They should keep revision sessions for regular intervals. They should make some crisp notes for the last minute reading and avoid unnecessary panic before the exam.</li>
                        </ul>
                                                
