                        <h2>SOMTHING ABOUT ELECTRONICS AND COMMUNICATION ENGINEERING</h2>
                        <p>Diploma In Electronics and Communication Engineering is a  diploma level Electronics and communication Engineering course.The focus of the course is on understanding electronics networks and devices, electric magnetic fields, computer fundamental, communications and control systems. Diploma in Electronics and Communication Engineering is a 3 year course. The academic program is divided into 6 semesters. </p>
                        <p>Admission to the Diploma In Electronics and Communication Engineering is based on merit in the qualifying examination followed by candidates performance in the counselling and interview round. The minimum eligibility criteria for the program is 10+2 qualified with a minimum score of 50% in aggregate with Physics, Chemistry and Mathematics as compulsory subjects.</p>
                        <p>The job positions offered to students after completion of the course is of a Production quality Manager, Electronics Hardware Development Engineer, Production Manager, Business development Manager Mechanical/ Electronics, Electronics  and Electrical Engineer, Electronics Technician, Product Line Engineers(Mechanical and Electronics) programmer Analyst, etc.</p>
