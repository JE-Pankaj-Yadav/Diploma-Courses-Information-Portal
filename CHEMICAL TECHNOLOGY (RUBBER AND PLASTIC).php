                        <h2>SOMTHING ABOUT CHEMICAL TECHNOLOGY (RUBBER AND PLASTIC)</h2>
                        <p>Chemical technologists and technicians (abbr. chem techs) are workers who provide technical support or services in chemical-related fields. They may work under direct supervision or may work independently, depending on their specific position and duties. Their work environments differ widely and include, but are not limited to, laboratories and industrial settings. As such, it is nearly impossible to generalize the duties of chem techs as their individual jobs vary greatly. Biochemical techs often do similar work in biochemistry.</p>
                        <h2>RUBBER</h2>
                        <P>Rubber Technology is the subject dealing with the transformation of rubbers or elastomers into useful products, such as automobile tires, rubber mats and, exercise rubber stretching bands. The materials includes latex, natural rubber, synthetic rubber and other polymeric materials, such as thermoplastic elastomers. Rubber processed through such methods are components of a wide range of items.</P>
                        <h2>Chemical Technology (Rubber and Plastic)</h2>
                        <p>The department of Rubber & Plastics Technology shall strive to be a globally renowned department known for its academic excellence, professionalism and social responsibilities. We aim to impart high technical knowledge, practical skill, leadership qualities, ethical skill, leader and foster creative entrepreneurial skill to become a demand based solution provider in the field of Rubber and Plastics Technology</p>
                        <h2>Mission of Rubber and Plastics Technology</h2>
                        <li>The mission of the Department of Rubber and Plastics Technology is to</li>
                        <li>Prepare the students to meet the fundamental expectations of the Rubber and Plastics industry</li>
                        <li>Enhance the technical knowledge with respect to the current scene through collaboration with industries and research organization</li>
                        <li>Emphasize Product Design aspects to enable students to be innovators in the field of Rubber and Plastics Technology</li>
                        <li>Encourage the students to become job providers rather than job seekers</li>
                        <li>Take up inter and multi disciplinary research and consultancy projects with industry and research establishments</li>
                        <li>Motivate the faculty members to do research and to be updated with the latest developments in the area of Rubber and Plastics Technology.</li>
                        <h2>Career Prospects</h2>
                        <li>You must know that rubber and plastic industry is already well-rooted in India and is rapidly growing. Various areas such as agriculture, telecom, transportation and construction depend largely upon rubber and plastic. In conjunction, since all these areas mentioned are on constant expansion, the growth in rubber and plastic industry is obvious.</li>
                        <li>A number of private and multi national companies engaged in the production and marketing of rubber or plastic goods provide nice job opportunities to rubber and plastic technologies and engineers.</li>
                        <li>After acquiring degree in rubber and plastic engineering, you can find job as plastic engineers, technologists and researchers. Even though the industry is growing successfully, but it still has lots more to do in India, so career opportunities in this field are not going to cease or decline but instead, more jobs will be there.  Thus, one can be sure of getting handsome salary and respectable designation</li>
