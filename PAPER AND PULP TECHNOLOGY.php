                        <h2>SOMTHING ABOUT PAPER AND PULP TECHNOLOGY</h2>
                        <p><b> Pulp and paper technology course is a branch of chemical technology which deals with the process of producing paper and its products from pulp, wood and other raw materials. The program emphasizes on fundamentals and applications of chemical sciences in pulp and paper manufacturing. It is an industry oriented course.</b> The program enables the students to learn about the manufacturing process of paper from the raw materials and process them with chemicals, heat, electricity etc. in order to make them marketable and usable for the consumers. The pulp and paper technology find its use in the paper industry including newspapers, publishers, packaging industry etc. and after completing the course, the students mainly find career opportunities in these industries.</p>
                        <p>The mission of this Program is to provide technologically advanced workforce to the pulp paper sector. This mission recognizes that the need for better-trained, skilled employees has become critical in staying competitive in today's global economy. As such, the Program provides up-to-date knowledge with the basic background to be effective employees in the pulp and paper industry or in industries that supply materials and products to the industry.</p>
                        <h2>EDUCATIONAL OUTCOMES:</h2>
                        <p>Upon successful completion of the Pulp and Paper Technology Program, the candidate is expected to:</p>
                        <ol>
                        	<li>Demonstrate competency in basic pulp and paper technologies as well as in math and related, fundamental technology skills.</li>
                        	<li>Be able to effectively communicate as well as have the ability to interface and work with peers and supervisors.</li>
                            <li>Be qualified to become quality employees in the pulp and paper or related industries.</li>
                        </ol>

                        <p><b>Career Prospects:</b></p>

                        <ul>
                        	<li>Manager Marketing</li>
                        	<li>Engineering Assistants</li>
                        	<li>Lab Personnel</li>
                        	<li>Line Supervisors</li>
                        	<li>Section In-charge</li>
                        	<li>Purchase Executive</li>
                        </ul><br>

