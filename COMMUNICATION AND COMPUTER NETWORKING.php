                        <h2>SOMTHING ABOUT COMMUNICATION AND COMPUTER NETWORKING</h2>
                        <p>Communication and Computer Networking play an important role in modern business and society. One cannot think of passing modern life and managing modern businesses without  Communication and Computer Networking.</p>
                        <p>Communication and Computer Networking is the utilization of science and math applied to practical problems in the field of computer science and Electronic communications. One cannot imagine daily life without a mobile phone, laptop (computer), television, tablets, digital watch, internet banking, ATM cards, Wi-Fi, internet connection, microwave Owen and many more gadgets and communication systems. As if one looks into the future, robots will also be part of our lives very soon and embedded electronics which is a subsidiary of Communication and  Computer Networking will play an important role in this.</p>
                        <p>A candidate aspiring for this course in Communication and Computer Networking will deal with the electronic devices, circuits, communication setup and will get to learn about computer science with programming, Basic and Advance networking, electronics and equipment like transmitter, receiver, PCBs, Microprocessors, Network programming languages, operating systems, data structure, and communication setup tools.</p>
                        <p>This course at BBDEC offers great career opportunities for the students with ample opportunities in industry with practical learning. The course is structured with a fine balance of theory classes where students develop the conceptual background and extensive hands-on training using Lab (practical exposure).</p>
                        <p>The Department aims at grooming students into global professionals and hence every student will have the opportunity to learn and understand cutting-edge technologies that have emerged in the recent past.</p>
                        <h2>Employment Opportunities:</h2>
                        <ul>
                            <li>Telecommunication Sector and related Departments </li>
                            <li>Service Division (IT-enabled services, Maintenance service, and Installation services)</li>
                            <li>Network installation and maintenance  </li>
                            <li>Wireless and Mobile communication </li>
                            <li>Electronic Components and Devices Manufacturing and Installation Organizations</li>
                            <li>Computer Assembling and Computer Peripheral Industry </li>
                            <li>Instrumentation and Control Industries </li>
                            <li>Mobile Phone assembly Industries</li>
                        </ul>
