                        <h2>SOMTHING ABOUT RENEWADLE ENERGY</h2>
                        <p>PG Diploma in Solar Renewable energy is an intensive one-year course taught in distance learning /Online learning mode. There are also two sessions of the Personal Contact Programme (PCP) modules for 5 days each every semester. This is held at one of our installed Solar Power plant locations around Mumbai. The one-year course with contact modules in Renewable courses distance learning is as per the enclosed syllabus of the pg diploma in energy management. The PG Diploma program is aimed at producing future leaders in the energy sector.</p>
                        <p>The global energy system is undergoing a process of rapid change. Industries, economies, and society in general face complex challenges and uncertainties that could become more extreme in the future. Both the government and industry need to be able to understand and adapt to the changing context of the world’s energy requirements. </p>
                        <p>Through this course, you’ll gain rigorous analytical training and in-depth real-world knowledge of global energy systems. There’s also hands-on training in the design, installation, and management of energy-related issues. Your post-graduate diploma in renewable energy in India training will help to give you an added advantage in the energy job market.</p>
                        <h2>The Solar Job Market</h2>
                        <p>A  Natural Resources Defense Council ( NRDC ) report makes clear that scaling up solar energy will not only address growing energy demands and climate change threats but also add as many as one million new engineers, technicians, solar installers, maintenance workers, and performance data monitors to its workforce.</p>
                        <p>The Report, filling the Skill Gap in India’s Clean Energy Market outlines the types of new jobs and new training facilities and institutes needed for India to reach its 100 GW solar energy target by 2022. Approximately 183,500 full-time jobs for Senior Management would be generated if India has to reach this target.</p>
                        <p>One of the biggest challenges for India’s booming renewable energy industry is getting skilled manpower. The primary goal of IISE is to fill this gap through solar short-term, long-term courses & solar workshops.</p>
                        <h2>Course Description</h2>
                        <p>The Post Graduate Diploma in Solar Renewable Energy program aims to understand the different aspects of renewable energy issues and their solutions.  This course is designed to offer flexibility and tailor-made to the market requirement with a deep understanding of the subjects.</p>
                        <p>The program enables students from across various industries, energy utilities, and consultancy firm to complete the course work without leaving their job or career. Students completing this course will be equipped with advanced and updated skills required to design, install, Commission, and evaluate the technical and economic viability of renewable energy and implement energy efficiency projects.</p>
