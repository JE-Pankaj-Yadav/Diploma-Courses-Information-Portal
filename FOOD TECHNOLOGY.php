                        <h2>SOMTHING ABOUT FOOD TECHNOLOGY</h2>
                        <p>Food Technology Courses cover basics of food production, food technology, microbiology and technologies involved in food quality testing. In order to pursue a career in Food Technology, one can pursue BSc, BTech, MSc, and MTech courses in food technology or food science specialization. Food Technology course eligibility is to complete 12th in science stream i.e. PCB, PCM, or PCMB with minimum 50-60% marks.</p>
                        <p>IIT Kharagpur, Jadavpur University Kolkata and NIT Rourkela are Top Food Technology Colleges in India. Admission to these courses are done on merit as well as entrance basis. JEE Main, JEE Advanced, COMED-K, ICAR and KCET are top entrance exams accepted for admission in food technology courses after 12th. </p>
                        <p>One can apply for various jobs in the field of Food Technology such as Lab Technician, Biochemist, Food Technologist, Production Manager, Home Economist, etc. A food technologist in India earns an average of INR 4,18,000 per annum as per the reports of Payscale. A food technologist at entry level can expect to earn an average of INR 4,00,000 per annum.</p>
                        <h2>All About Food Technology Courses</h2>
                        <p>Food Technology is a branch of science which deals with various methodology and techniques involved in production, processing, preservation, packaging, labelling, quality management, and distribution of food products. They boost the quality of food products available.</p>
                        <ul>
                        	<li>As per the report of IBEF, the Indian food processing industry is ranked fifth in terms of production, consumption, export and expected growth and consists of 32% of the country's food market.</li>
                        	<li>Moreover, the Indian food processing sector is among the largest in the world and by 2025-26, it is expected to reach INR 40 thousand billion. It is also expected that by 2024, it'll generate 9 million jobs.</li>
                        	<li>Food Technology Courses in diploma, undergraduate, postgraduate can be pursued after completion of 12th or an equivalent level. However, some of the Certification courses can be pursued after completion of 10th.</li>
                        	<li>The duration of the course can be upto 4 years depending on the type of course.</li>
                        	<li>The course fees for various Food Technology courses can be anywhere between INR 10,000 - 12,00,000.</li>
                        	<li>Indian Institute of Food Processing Technology, Laxminarayan Institute of Technology, Jamia Hamdard University, Gautam Buddha University, Galgotias University, Lovely Professional University, etc are some of the colleges which offer food technology courses.</li>
                        	<li>After completing Food Technology courses, one can apply for various jobs Lab Technician, Biochemist, Food Technologist, Production Manager, Home Economist, etc.</li>
                        	<li>The average salary in the field of food technology ranges between INR 3,00,000 - 8,00,000 per annum.</li>
                        </ul>
                        <h2>Food Technology Courses Eligibility</h2>
                        <p>Basic food technology course eligibility criteria is to complete class 12 with minimum qualifying marks (50 &ndash;&nbsp;60%). Eligibility criteria for different types of food technology courses is listed below.</p>

                        <ul>
                        	<li>The basic food technology course eligibility is to complete class 10 (for certificate or Diploma in Food Technology) or class 10+2 (for bachelors, masters and PhD in Food Technology) in Science Stream.</li>
                        	<li>Food Technology Subjects that are required to take admission in bachelors, masters, or PhD in food technology include Biology, Physics, Chemistry, Food Science and also Maths (in case of BTech or MTech in food technology).</li>
                        	<li>To apply for masters in food technology, one must complete BSc/BTech in Food Technology with minimum qualifying marks. Government Food Technology Colleges require more than 90% marks in graduation.</li>
                        	<li>In order to apply for PhD in Food Technology, one must complete MSc/MTech food technology with minimum qualifying marks, research proposals, and qualifying PhD Entrance Exams.</li>
                        	<li>JEE Main, JEE Advanced, COMED-K, ICAR, KCET, GATE are popular exams accepted for food technology courses after 12<sup>th</sup>.</li>
                        </ul>
