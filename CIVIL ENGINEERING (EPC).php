                        <h2>SOMTHING ABOUT CIVIL ENGINEERING (ENVIRONMENT & PULLUTION CONTROL)</h2>
                        <p>Environmental engineering is a professional engineering discipline that encompasses broad scientific topics like chemistry, biology, ecology, geology, hydraulics, hydrology, microbiology, and mathematics to create solutions that will protect and also improve the health of living organisms and improve the quality of the environment. Environmental engineering is a sub-discipline of civil engineering and chemical engineering. While on the part of civil engineering, the Environmental Engineering is focused mainly on Sanitary Engineering.</p>
                        <p>Environmental engineers conduct hazardous-waste management studies to evaluate the significance of such hazards, advise on treatment and containment, and develop regulations to prevent mishaps. Environmental engineers also design municipal water supply and industrial wastewater treatment systems as well as address local and worldwide environmental issues such as the effects of acid rain, global warming, ozone depletion, water pollution and air pollution from automobile exhausts and industrial sources.</p>
                        <h2>Pollution.</h2>
                        <p>Pollution is the introduction of contaminants into the natural environment that cause adverse change. Pollution can take the form of any substance (solid, liquid, or gas) or energy (such as radioactivity, heat, sound, or light). Pollutants, the components of pollution, can be either foreign substances/energies or naturally occurring contaminants. Although environmental pollution can be caused by natural events, the word pollution generally implies that the contaminants have an anthropogenic source – that is, a source created by human activities. Pollution is often classed as point source or nonpoint source pollution. In 2015, pollution killed nine million people worldwide (one in six deaths). This remained unchanged in 2019, with little real progress against pollution being identifiable. Air pollution accounted for ¾ of these earlier deaths.</p>
                        <h2>Civil Engineering (Environment & Pollution Control)</h2>
                        <p>Environmental engineering is a professional engineering discipline that takes from broad scientific topics like chemistry, biology, ecology, geology, hydraulics, hydrology, microbiology, and mathematics to create solutions that will protect and also improve the health of living organisms and improve the quality of the environment.Environmental engineering is a sub-discipline of civil engineering, chemical engineering and mechanical engineering.</p>
                        <p>Environmental engineering is the application of scientific and engineering principles to improve and maintain the environment to:</p>
                        <li>protect human health,</li>
                        <li>protect nature's beneficial ecosystems,</li>
                        <li>and improve environmental-related enhancement of the quality of human life.</li>
