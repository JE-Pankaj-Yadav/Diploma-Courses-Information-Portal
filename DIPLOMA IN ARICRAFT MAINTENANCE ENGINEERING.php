                        <h2>SOMTHING ABOUT DIPLOMA IN ARICRAFT MAINTENANCE ENGINEERING</h2>
                        <p>Diploma in Aircraft Maintenance Engineering (AME) is a 3 years diploma course having six semesters. Each semester comprises of 6 months approved by the All India Council for Technical Education (AICTE). AME course involves studying, designing, developing, and testing of aircraft. The safety and security of aircraft is in hands of AME. AME must have good problem solving skills and passionate about learning about aircraft.</p>
                        <p>An Aircraft Maintenance Engineer (AME), also Licensed Aircraft Maintenance Engineer (LAME or L-AME), is a licensed person who carries out and certifies aircraft maintenance. The license is widespread internationally and is recognised by the International Civil Aviation Organization (ICAO).[1] The American FAA recognise the qualification in foreign countries but refer to it as Aviation Maintenance Engineer rather than "Aircraft...</p>
                        <p>Countries which issue or recognize AME licenses internally include; Australia, Bangladesh, Canada, India, Ireland, New Zealand, the United Kingdom and much of Asia.</p>
                        <h2>Diploma in Aircraft Maintenance Engineering Eligibility Criteria</h2>
                        <p>The eligibility criteria of the diploma in aircraft maintenance engineering (AME), for the desired candidates, are given below:-</p>
                        <li>The candidate must be passed or pursue their 10th board examination.</li>
                        <li>The candidate must be scored 45% marks in their 10th board examination.</li>
                        <li>The candidate does not have any physical disability.</li>
                        <li>The candidate does not have any color blindness.</li>
                        <li>Applicants can qualify for the AME CET to get admission in a diploma in AME.</li>
                        <h2>Diploma in Aircraft Maintenance Engineering Course Duration</h2>
                        <p>Diploma in AME is a 3-years engineering program that offers theoretical and practical knowledge about the maintenance, repair, testing, and solving the issues in an aircraft. The theoretical knowledge will be gathered in the modern classroom and practical understanding in well-equipped labs. After completion of the course, student will get the diploma certificate from the college.</p>
                        <h2>Diploma in Aircraft Maintenance Engineering Career Opportunities</h2>
                        <p>Indian aviation sector is growing so quickly which leads to high requirement of manpower. The well- being and protection of an aircraft places an essential role in the aviation sector. The job opportunities are mushrooming these days when it comes to AME. Diploma in Aircraft Maintenance Engineering is a job-oriented diploma program that offers student to build their fascinating career in the aviation sector.</p>
                        <h2>There are some career opportunities that the candidate can grab after completing the diploma. Furthermore, the same have been given below:-</h2>
                        <li>Airlines</li>
                        <li>Maintenance Repair and Overhauls (MRO) industries</li>
                        <li>Aircraft Manufacturing Companies</li>
                        <li>Aircraft Part Manufacturing Companies</li>
                        <li>Aircraft Operation Organizations</li>
                        <li>Aviation Training Centers</li>
                        <h2>Diploma in Aircraft Maintenance Engineering Fees structure</h2>
                        <p>Diploma in AME is a 3 years of the duration program. Candidate has to submit their fees at AME colleges which is around INR 2 Lakhs to 4 Lakhs as a total diploma program fee. The fees depend on the institute which the applicant will choose in AME CET counseling. The candidate has to pay the fee semester wise. Students can apply for the bank loan facilities. Hostel facilities can be opted on the basis of requirements.</p>
