                        <h2>SOMTHING ABOUT PG DIPLOMA IN FASHION TECHNOLOGY</h2>
                        <p>National Institute of Fashion Technology (NIFT) is an autonomous institute that offers courses in fashion, designing, technology, and management. Its head office is located in New Delhi, India.</p>
                        <p>Indian Institute of Fashion Technology also known as IIFT was established in the year 2001 in Bangalore, Karanataka by BVG Educational Trust. The branches of Indian Institute of Fashion technology are spread across Karnataka. The Fashion Institute provides various courses in the field of Fashion.</p>
                        <p>Indian Institute Of Fashion Technology entered Limca Book Of Records in 2011 for displaying most designs at Fashionite 2010. The Institute participates in various events including Charity as well as Fashion Weeks. Indian Institute of Fashion Technology was the Educational partner for Bangalore Fashion Week 2020. IIFT College bagged the 1st rank & 8th rank in the year 2012 and the 8th rank in the year 2013 in the Bangalore University for B.Sc Fashion and Apparel Design.</p>
                    </div><br>
                    <h4>Do I qualify for a Post Graduate Diploma in Fashion Designing eligibility Criteria?</h4>
                    <li>The minimum requirement of eligibility criteria for pursuing the program is mentioned below. The willing candidates are required to fulfill the following requirement to be eligible to apply for the program: </li>
                    <li>Completion of bachelor's degree in fashion designing or a bachelor's degree (equivalent) with 50% marks from a university recognised by UGC is essential.</li>
                    <li>The percentage required will vary from university to university. A student who has successfully finished a diploma of 3 year duration, in any stream from a recognised university can also apply for a PG diploma in fashion designing.</li>
                    <li>Some colleges give admission to students on merit basis but some have entrance exams.</li><br>
                    <h4>What is the Post Graduate Diploma in Fashion Designing Admission Process?</h4>
                    <p>Admissions in Post Graduate Diploma courses are also done through both merit and entrance exams.</p>
                    <h5>Direct Admission</h5>
                    <li>Candidates who are willing to get admission in this stream can apply to Post Graduate Diploma in Fashion Designing colleges by both offline and online means.</li>
                    <li>You can either reach the official website of the institute or to this step admission office of the institute to fill the admission application form.</li>
                    <li>Fill the application form required and upload all the necessary documents which are asked in the form.</li>
                    <li>After completion of the process of application, go for counselling round and attend the personal interview round to get your desired college.</li><br>
                    <h4>Post Graduate Diploma in Fashion Designing: Course Advantages</h4>
                    <p>The PG diploma in fashion designing aims to teach the student the basics of creating clothing and preparing the student to cater to a range of people.</p>
                    <li>In PG diploma in fashion designing, the students learn about colours, fabrics, creation and construction of dresses. They also learn about pattern making and surface ornamentation. Fashion accessories are also taught.</li>
                    <li>The technical expertise of dress making is taught. The students are also taught about traditional local textile so that they can create new innovative designs in which they mould the elements of traditional dresses according to current western trends and create fusion wear.</li>
                    <li>The student develops the technical know-how to start a business in garments or open his or her own boutique or start a brand.</li>
                    <li>The students learn to develop interpersonal and social skills when he interacts with industry professionals during internship.</li>
                    <li>The confidence of the student is boosted as he walks on the ramp with the fellow students who are showcasing his designs. Good presentability and confidence are key for success in any industry and in a dazzling and competitive industry like fashion, it is a must.</li>
                    <li>Industry expertise - In reputed colleges many famous designers and stylists are invited for guest lectures. Their piercing insights help the students know more about the industry they want to go in. Some designers may even share a series of Do's and Don'ts so that they don't make the common mistakes.</li><br>
                    <h4>Post Graduate Diploma in Fashion Designing: Tips to get Best College</h4>
                    <p>To get admission into a good college one needs to ensure to have scored more than 90% at the Intermediate level. Apart from this, most of the institutes provide admission to the course on the basis of entrance examinations, hence preparing well for the entrance exams is a must for an aspirant to get the best college.</p>
                    <p>Note: Before shortlisting and selecting colleges finally, they must ensure to check the placement rate of the college, method of course delivery, admission system, and the financial aid offered there.</p>
