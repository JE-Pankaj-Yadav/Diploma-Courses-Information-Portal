                        <p><b>Key points regarding admission</b>:</p>
                        
                        <ul>
                        	<li>On the receipt of the completed application form along with the registration fee, candidates will be registered with the institute for Diploma/ Certificate course and will be allotted a registration number. They will need to quote this registration number for future reference.</li>
                        	<li>Minimum duration of completing Diploma courses is 3 years and for completing certificate course is six months.</li>
                        	<li>In the case of expiry of the period of three years/ six months (as applicable), the candidates not being able to complete the course will have to register again for which a nominal registration fee will be charged.</li>
                        </ul>
                        <h2>IICT Bhadohi Application Process 2022</h2>
                        <p>Candidates can apply for the courses through offline mode. They can do so by filling the form available at the admission window of the institute. The application fee for the form is INR 250 which can be paid in cash.</p>
                        
                        <p>They can also download the form by following these steps:</p>
                        
                        <ul>
                        	<li>Visit the official website of IICT Bhadohi.</li>
                        	<li>Click on &ldquo;Courses&rdquo; tab</li>
                        	<li>Select the course for which you want to apply</li>
                        	<li>Click on &ldquo;Application Form&rdquo; link</li>
                        	<li>Take the print of the form and fill the necessary details.</li>
                        	<li>Send the duly filled in application form along with the required documents to the institute.</li>
                        	<li>The application fee for the downloaded forms is INR 300.</li>
                        	<li>The application fee will have to be paid through Demand Draft drawn in favor of &ldquo;Indian Institute of Carpet Technology&rdquo; payable at Badohi.</li>
                        </ul>
