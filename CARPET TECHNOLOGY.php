                        <h2>SOMTHING ABOUT CARPET TECHNOLOGY</h2>
                        <p>Indian Institute of Carpet Technology (IICT) started functioning from 2001 and is located in Bhadohi, Uttar Pradesh. IICT is affiliated to Uttar Pradesh Technical University, Lucknow (APJAKTU) and is approved by AICTE.</p>
                        <p>IICT Bhadohi offers B.Tech, Short Term and Distance Education courses (Diploma and Certificate) in the spectrum of fields of Carpet Technology. The annual intake for the B.Tech program is 60 students.</p>
                        <ul>
	                        <li>The applications can be submitted through online and offline mode.</li>
	                        <li>Candidates must have completed 10+2 level education for admission to regular courses (B.Tech and Short-Term).</li>
	                        <li>For the distance education courses, candidates must have completed High School (Class 10<sup>th</sup>).</li>
	                        <li>Admissions to B.Tech program are based on candidates&rsquo; rank in Joint Entrance Exam (JEE) Main.</li>
	                        <li>Admissions to other courses are offered on first come, first serve basis.</li>
                        </ul>
