                        <h2>IICT Bhadohi B.Tech Admission 2022</h2>
                        <p>The institute offers B.Tech in the branch of Carpet Textile Technology. The institute provides the following specializations in the final year: Home Textile Technology, Advanced Textile Technology, Textile Designing Technology.</p>
                        
                        <p>The minimum qualification for applying is 60% marks in 10+2 with Physics, Mathematics as compulsory subjects along with Chemistry/ Biology/ Biotechnology from CBSE or any recognized board.</p>
                        
                        <p>All the admissions to B.Tech program are done through JEE Main and counselling by Central Counseling Board (CCB), Ministry of HRD, Government of India.</p>
                        
