                        <h2>Important points:</h2>
                        
                        <ul>
                        	<li>Relaxation of 10% will be given for SC/ST candidates in the qualifying examination.</li>
                        	<li>Class 12<sup>th</sup> marks will not be added for calculation of rank and merit.</li>
                        	<li>For Lateral Entry admission, the candidates must have an Indian nationality and should have passed Diploma in Engineering or Technology with at least 45% aggregate marks.</li>
                        	<li>Candidates are also eligible to apply if they have passed B.Sc degree from UGC recognized institution with at least 45% marks and passed 10+2 with Mathematics as one of the subjects and with English as the medium of instruction.</li>
                        </ul>
                        <h2>IICT Bhadohi Short Term Courses Admission 2022</h2>
                        <p>IICT Bhadohi offers four-month short term courses under four disciplines in the field of Carpet Technology. Candidates must have passed 10 with Mathematics as a compulsory subject.</p>
                        
                        <p>Admissions to these courses are based on first come first serve basis. Admissions are conducted thrice a year- August to November, December to March and April to July session.</p>
                        
                        <p>Application forms are available both online and offline. Eligible candidates can either download the form from the portal and send to the institute along with the Demand draft or can obtain personally from the admission office at the college.</p>
                        
