                        <h2>SOMTHING ABOUT DIPLOMA IN MASS COMMUNICATION</h2>
                        <p>Mass communication is the process of imparting and exchanging information through mass media to large segments of the population. It is usually understood for relating to various forms of media, as its technologies are used for the dissemination of information, of which journalism and advertising are part. Mass communication differs from other types of communication, such as interpersonal communication and organizational communication, because it focuses on particular resources transmitting information to numerous receivers. The study of mass communication is chiefly concerned with how the content of mass communication persuades or otherwise affects the behavior, the attitude, opinion, or emotion of the people receiving the information.</p>
                        <p>Normally, transmission of messages to many recipients at a time is called mass communication. But in a complete sense, mass communication can be understood as the process of extensive circulation of information within regions and across the globe.</p>
                        <p>Through mass communication, information can be transmitted quickly to many people who generally stay far away from the sources of information. Mass communication is practiced multiple mediums, such as radio, television, social networking, billboards, newspapers, magazines, books, film, and the Internet. In this modern era, mass communication is being used to disperse information at an accelerated rate, often about politics and other charged topics. There are major connections between the media that is being consumed, via mass communication, and our culture, contributing to polarization and dividing people based on consequential issues.</p>
