                        <li>Journalism is the activity of gathering, creating, evaluating, and presenting information in the form of news and articles. However, mass communication refers to the process of disseminating and exchanging information to large segments of the population via mass media.</li>
                        <li>Students who have a keen interest and passion for writing are considered the most suitable students for such courses. This course helps the students to gain a piece of in-depth knowledge about the media and nuances of journalism.</li>
                        <li>It is clear that in recent years, the media has become an integral part of the nation in all possible ways, including newspapers, radio, televisions, and so on.</li>
                        <li>The aspiring students must possess good communication skills, interpersonal skills and even creative skills which might help them in pursuing this particular course. </li>
